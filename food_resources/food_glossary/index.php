                            <a name="roast"></a>
                            roast
                        </dt>
                        <dd>
                        <p>Roasting is a <a href="#dry_heat">dry heat</a> cooking method. It is physically equivalent to <a href="#bake">baking</a>, the entry for which contains more detail.</p>
                        </dd>

                        <dt>
                            rolling boil
                        </dt>
                        <dd>
                            <p>See <a href="#full_boil">full boil</a>.</p>
                        </dd>

                        <dt>
                            <a name="roux"></a>
                            roux
                        </dt>
                        <dd>
                            <p>A roux (roughly &quot;ROO&quot;)is a <a href="#thicken">thickening</a> made of one <a href="#ratio">part</a> flour and one part fat, both measured by weight. Roux (plural, spelled and pronounced the same the same as the singular) are described by color from lightest to darkest: white, blonde, brown, and &quot;black&quot;, although if it gets <strong>that</strong> dark, it's going to be unpleasant and bitter. Beware the <a href="#carryover_cooking">carryover</a> effect.</p>
                            <p>To make a roux, whisk the fat and flour over medium high heat in a saut&eacute; pan. Mix fairly frequently while it bubbles and cooks. The roux will begin to smell like fresh bread, and at this stage it is considered white. Continue to cook and stir until it starts to smell toasty but hasn't begun to change color very much: Blonde. Cooked further to a golden brown or a little darker: Brown. And cooked until it's a deep, mahogany brown, you have a black or Cajun roux.</p>
                            <p>A pound of blonde or white roux&mdash;8 ounces each of fat and flour&mdash;will thicken a gallon of stock to a medium consistency. Darker roux won't thicken as well, so plan to use more of it for the same level of thickness.</p>
                        </dd>

                        <dt>
                            <span class="glossary_letter"><a name="S_">S</a></span><br />
                            <a name="sachet"></a>
                            sachet
                        </dt>
                        <dd>
                            <p>A sachet is a bundle of herbs and other <a href="#flavor">flavorings</a> added to a <a href="#stock">stock</a>, sauce, etc. while it cooks, then removed at the end. A sachet can be bundled in cheesecloth, tied together with string, loosely packed into a tea ball, or&mdash;if whatever pureeing and straing of the final result allows it&mdash;simply left in after all.</p>
                            <p>&quot;Standard&quot; sachets vary, but for a gallon of stock, a sachet might include 8 to 12 black peppercorns, 1 teaspoon of dried thyme, 4 to 6 bruised parsley stems or some root, a large bay leaf, and two garlic cloves peeled and lightly crushed. Some books and other sources call fora clove or two as well.</p>
                        </dd>

                        <dt>
                            <a name="saute"></a>
                            saut&eacute;
                        </dt>
                        <dd>
                            <p>Saut&eacute; comes from the French word <span class="foreign">sauter</span>, meaning &quot;to jump&quot;. It is a <a href="#dry_heat">dry heat</a> cooking method characterized by farily high heat and a little <a href="#fat">fat</a> in a pan. Saut&eacute;ing is one of the most versatile cooking methods available and it works as well for cooking thin cuts of meat or <a href="#brown">browning</a> them for <a href="#braise">braising</a> as it does for vegetables (<a href="#blanch">blanched</a> as necessary) tossed in hot olive oil with garlic, salt, and herbs for service, or <a href="#caramelize">caremelized</a> with tomato for a <a href="#brown_sauce">brown sauce</a>.</p>
                        </dd>

                        <dt>
                            <a name="savory"></a>
                            <a name="umami"></a>
                            savory or <span class="foreign">umami</span>
                        </dt>
                        <dd>
                            <p>Savory is one of the elements of <a href="#flavor">flavor</a> that humans can detect, along with salt, sweet, bitter, and sour. It is present in the food additive monosodium glutamate (MSG) and in condiments such as ketchup and soy sauce. A good <a href="#stock">stock</a> is rich in savory <a href="#flavor">flavor</a> from the breakdown of the <a href="#collagen">collagen</a> as it cooks.</p>
                        </dd>

                        <dt>
                            <a name="seasoning"></a>
                            seasoning
                        </dt>
                        <dd>
                            <p>Seasonings, or seasoning agents, enhance flavors already present in a food. When you add white pepper and salt to finish up <a href="#bechamel">B&eacute;chamel</a>, for example, you're not looking for saltiness or peppery heat, you're looking for the sauce to taste more like itself than it does. Fresh slices of tomatoes, sprkinked with just a little salt and left alone for about 10 minutes, taste much better than their unsalted counterparts: Not salty, just more like tomatoes.</p>
                            <p>See also <a href="#flavor">flavor</a>.</p>
                        </dd>

                        <dt>
                            <a name="shock"></a>
                            shock
                        </dt>
                        <dd>
                            <p>To shock an item is to immediately stop its <a href="#carryover_cooking">carryover cooking</a> by application of cold temperatures, typically as ice water. If you are <a href="#blanch">blanching</a> carrots to include later in a medley with broccoli and yellow squash, you should drain and shock them to keep them from cooking further. In a commercial kitchen, a blast chiller can be used to shock foods that don't do well submerged in water.</p>
                        </dd>

                        <dt>
                            <a name="simmer"></a>
                            simmer
                        </dt>
                        <dd>
                            <p>Simmering is a <a href="#moist_heat">moist heat</a> cooking method characterized by cooking food submerged in <a href="#water">water</a> at a fairly lively but not <a href="#full_boil">full</a> boil. Simmering temperatures are higher than those for <a href="#poach">poaching</a> and tend to be in the 185&deg;F/85&deg;C to 205&deg;F/96&deg;C range.</p>
                        </dd>

                        <dt>
                            <a name="slurry"></a>
                            slurry
                        </dt>
                        <dd>
                            <p>A slurry is a purified <a href="#starch">starch</a> such as cornstarch, potato starch, or arrowroot powder, mixed with water, and used as a thickening agent. When you mix in the slurry and bring the liquid up to a <a href="#full_boil">boil</a>, the starch <a href="#geletanize">geletanizes</a>, binding the starch molecules to each other and thickening the liquid.</p>
                            <p>A slurry made with flour is called a <a href="#whitewash">whitewash</a> and its use is generally discouraged.</p>
                        </dd>

                        <dt>
                            <a name="small_sauce"></a>
                            <a name="child_sauce"></a>
                            small sauce or child sauce
                        </dt>
                        <dd>
                            <p>Small sauces are simply the collection of sauces derived from the five <a href="#mother_sauce">mother sauces</a>. <a href="#hollandaise">Hollandaise</a> is a mother (or leading sauce), for example, and its small sauces include Mousseline and Bearnaise.</p>
                        </dd>

                        <dt>
                            <a name="smoke"></a>
                            smoke
                        </dt>
                        <dd>
                            <p>Smoking is a <a href="#dry_heat">dry heat</a> cooking method characterized by surrounding the food being cooked with wood, charcoal, or other smoke. Smoking is also a preservative technique, and items may be hot-smoked&mdash;smoked at temperatures close to <a href="#roast">roasting</a>&mdash;or cold-smoked at temperatures as low as 150&deg;F/65&deg;C.</p>
                            <p>Smoke itself should be considered a <a href="#flavor">flavoring</a> ingredient: Whether a sausage is hot- or cold-smoked, the smoke add flavor. &quot;Liquid&quot; is available at most supermarkets and is usually simply that: Naturally produced smoke, captured, and condensed to a liquid. With a gentle hand&mdash;the turkey legs sold at <a href="http://www.hokiesports.com/football/lanestadium.html" class="external" target="_blank" title="External link. Opens in new window.">Lane Stadium</a> are usually too heavy with it&mdash;you can bring some smokey <a href="#flavor">flavor</a> to a dish without even lighting a match.</p>
                        </dd>

                        <dt>
                            <a name="smoke_point"></a>
                            smoke point
                        </dt>
                        <dd>
                            <p>The temperature at which <a href="#oil">oil</a> starts to break down and smoke. Different oils smoke at different temperatures, so their uses in the kitchen vary. Some extra virgin olive oils, for example, can start to smoke as temperatures as low as 300&deg;F/150&deg;C. What's great on a salad may not be right for <a href="#deep_fat_fry">deep frying</a>. Peanut oil, on the other hand, does well at temperatures a little above 400&deg;F/200&deg;C, so it's perfect for making french fries.</p>
                            <p>If oil starts to smoke, it's too hot and should be removed from the heat <strong>immediately</strong>. Smoking oil can add unpleasant flavors. The smoke is also your last warning before it <strong><a href="#flash_point">bursts into flame</a></strong>.</p>
                            <p>Frying oil does have a limited useful lifetime, and contaminants&mdash;breading or crumbs that fall in, salt, etc.&mdash;will shorten the time it has. If you're careful to keep your oil as clean as you can, though, you can usually cool and strain it for another use or two.</p>
                        </dd>

                        <dt>
                            <a name="starch"></a>
                            starch
                        </dt>
                        <dd>
                            <p>A starch is a <a href="#carbohydrate">carbohydrate</a> composed of large collections of simple <a href="#sugar">sugars</a>. At a small enough scale, the molecules resemble long chains, hairy baseballs, or some combination of the two.</p>
                            <p>Starches are the primary nutritional ingredients in breads and potatoes, and they make up a great deal of the material in beans, nuts, and other such high <a href="#protein">protein</a> or high <a href="#fat">fat</a> plant-based foods.</p>
                            <p>When isolated and purified&mdash;cornstarch or arrowroot, for example&mdash;they can be used in a <a href="#slurry">slurry</a> to <a href="#thicken">thicken</a> sauces and gravies.</p>
                        </dd>

                        <dt>
                            <a name="steam"></a>
                            steam
                        </dt>
                        <dd>
                            <p>Steam refers to <a href="#water">water</a> in its gaseous form. We often talk about seeing steam above a boiling pot or when you open a hot dishwasher, but it's technically incorrect. What you're seeing is water in liquid form&mdash;small droplets of it&mdash;as it condenses in the air (or on your glasses). Steam itself is invisible.</p>
                            <p>That's an important point, particularly with regards to safety. Steam is <strong>Hot</strong>&mdash;potentially much hotter than water's <a href="#boiling_point">boiling point</a>&mdash;and should be respected. Be careful removing a lid from a boiling pot or opening a package from the <a href="#microwave">microwave</a>.</p>
                            <p>Steaming food is a <a href="#moist_heat">moist heat</a> cooking method. Because water is such an efficient <a href="#conduction">conductor</a> of heat, cooking in steam is typically faster that, say, <a href="#roast">roasting</a> in a dry oven. </p>
                        </dd>

                        <dt>
                            <a name="stew"></a>
                            stew
                        </dt>
                        <dd>
                            <p>See <a href="#braise">braise</a> for more detail. Stewing and braising are really the same cooking method. We just tend to use &quot;stew&quot; when the pieces of food are smaller.</p>
                        </dd>

                        <dt>
                            <a name="stock"></a>
                            stock
                        </dt>
                        <dd>
                            <p>A stock is, at its simplest, a clear, flavorful liquid, extracted from bones and meat. Stocks can also be made from vegetables.</p>
                            <p>Stocks are often classified as &quot;white&quot; or brown stock, the difference being the handling of the bones and <a  href="#mirepoix">mirepoix</a> that go into it. Brown stocks are made from roasted bones&mdash;veal, beef, or chicken usually&mdash;while white stocks&mdash;made from fish (often called &quot;fumet&quot; rather than &quot;stock&quot;), unroasted chicken or veal&mdash;are intentionally kept lighter in color and flavor.</p>
                            <p>Stocks are seldom served on their own, but they are essential to great cooking. If you <a href="#braise">braise</a> shredded green cabbage in chicken stock for 8 to 10 minutes with a little salt and pepper, for example, you might discover a taste for cabbage. Stocks are the bases for soups and sauces. They're great for <a href="#simmer">simmering</a> or <a href="#poach">poaching</a> other foods. Clarified further to a consomm&eacute;, they can be stunning and elegant.</p>
                        </dd>

                        <dt>
                            <a name="sugar"></a>
                            sugar
                        </dt>
                        <dd>
                            <p>The term sugar often refers to table sugar, sucrose, C<sub>6</sub>H<sub>12</sub>O<sub>6</sub>, composed of one molecule each of simpler sugars Glucose and Fructose. There are other common sugars we encounter in cooking, among them Maltose, Galactose, and the Lactose that is found in milk. Discussion of the individual sugars, their relative sweetnesses, and their other properties is more than can nicely fit in a glosary entry.</p>
                            <p>For cooking and nutrtional purposes, sugar is a <a href="#carbohydrate">carbohydrate</a>. Sugars are far simpler in structure than <a href="#starch">starches</a>, however, and they dissolve in water rather than <a href="#geletanize">geletanizing</a>.</p>
                        </dd>

                        <dt>
                            <a name="sweat"></a>
                            sweat
                        </dt>
                        <dd>
                            <p>To sweat vegetables, as <a href="#mirepoix">mirepoix</a> headed for a sauce or soup might be, cook them over low heat with just enough fat to coat, until they are soft. Onions, celery, and similar vegetables will become transluscent or clear. If it's sizzling, the heat is proably too high. The goal of sweating, unlike <a href="#caramelize">caramelization</a>, is to coax out the juices and flavors without changing them much.</p>
                        </dd>

                        <dt>
                            <span class="glossary_letter"><a name="T_">T</a></span><br />
                            thicken
                        </dt>
                        <dd>
                            <p>Thickening a liquid&mdash;making it so it's thicker, or less viscous, so it doesn't flow as easily&mdash; is can be done in a few different ways.</p>
                            <p>The easiest one to conceptualize may be the method that's used in <a href="#tomato">Tomato Sauce</a>; i.e., a vegetable puree gives sufficient body that the resulting sauce is not thin and watery.</p>
                            <p>The other two are direct applications of the fact that <a href="#starch">starches</a> <a href="#geletanize">geletanize</a> in hot water. Liquids can be thickened with a <a href="#slurry">slurry</a> or a <a href="#roux">roux</a>. With those methods, the starch molecules swell up and grab on to each other.</p>
                            <p>See also <a href="#napper">napper</a>.</p>
                        </dd>

                        <dt>
                            <a name="tomato"></a>
                            Tomato Sauce
                        </dt>
                        <dd>
                            <p>Tomato Sauce is one of the five <a href="#mother_sauce">mother sauces</a>. It's essentially a cooked tomato puree with other flavors. While it <strong>can</strong> be enriched with <a href="#stock">stock</a> or <a href="#thicken">thickened</a> with a <a href="#roux">roux</a>, it's not particularly usual to do so.</p>
                            <p>It's also incredible easy to make from scratch. Start with a pound of <a href="#mirepoix">mirepoix</a>, maybe a little salt pork or bacon, and <a href="#sweat">sweat</a> or <a href="#caramelize">caramelize</a> it as you prefer. Add herbs and other flavors you like&mdash;a quarter pound of parmesan cheese (or a leftover rind or two), some fresh fennel or fennel seed, garlic, oregano, thyme, rosemary, maybe a little sugar or a splash of vodka. Add a #10 can&mdash;about a gallon&mdash; of tomato. Puree, crushed, chopped; any form works. <a href="#simmer">Simmer</a> for half an hour to 45 minutes, puree, and adjust seasonings. To that, you can add browned ground beef or sausage, slices of mushroom, or fresh basil as you like.</p>
                            <p>This is a big batch, so plan to store leftovers. It'll keep in the fridge for at least a week, and you can freze it for considerably longer than that.</p>
                            <p>You can certainly use fresh tomatoes&mdash;peel and remove the seeds&mdash;if you have good ones from your garden or the farmers market. Grocery store tomatoes, though, are grown for their durability during shipping, not flavor. They tend to have little natural tomato <a href="#flavor">flavor</a> or acid, and the sauce just doesn't reach its potential.</p>
                            <p><a href="#small_sauce">Small sauces</a> include ketchup, marinara, Creole with green pepper and garlic, and the sort of barbecue sauce you'll see in tomato-based lands, with sugar or molasses, vinegar, and onion.</p>
                        </dd>

                        <dt>
                            <span class="glossary_letter"><a name="U_">U</a></span><br />
                            <span class="foreign">umami</span>
                        </dt>
                        <dd>
                            <p>See <a href="#savory">savory</a>.</p>
                        </dd>

                        <dt>
                            <span class="glossary_letter"><a name="V_">V</a></span><br />
                            <a name="veloute"></a>
                            Velout&eacute;
                        </dt>
                        <dd>
                            <p>Sauce Velut&eacute; is one of the five <a href="#mother_sauce">mother sauces</a>. It is just like <a href="#brown_sauce">Brown Sauce</a> in the sense that it is a <a href="#stock">stock</a> <a href="#thicken">thickened</a> with a <a href="#roux">roux</a>.</p>
                            <p>It's also different. First it's made with a white stock&mdash;chicken or veal, or fish fumet. Second, the <a href="#mirepoix">mirepoix</a> is <a href="#sweat">sweated</a> rather than <a href="#caramelize">caramelized</a>.</p>
                            <p><a href="#small_sauce">Small sauces</a> include Allemande: Veal Velout&eacute; with egg yolks and heavy cream., Supr&ecirc;me: Chicken Velout&eacute; with Cream. White Wine: Fish Velout&eacute; with dry white wine and cream. Any of those can be further transformed into a Mushroom sauce with mushrooms and a little lemon. (There's also a brown stock Mushroom made from demi-glace, mushrooms, and shallots.)</p>
                            <p>To a gallon of Velout&eacute; Add 5 or 6 pounds of chopped broccoli, asaparagus, or butternut squash. Simmer until soft. Puree, strain, adjust seasonings: Cream of Broccoli (or Asparagus or Butternut Squash) Soup.</p>
                        </dd>

                        <dt>
                            <span class="glossary_letter"><a name="W_">W</a></span><br />
                            <a name="water"></a>
                            water
                        </dt>
                        <dd>
                            <p>Water, H<sub>2</sub>O, is essential to life, and common in the kitchen in its liquid state as well as in its solid (ice) and gaseous (<a href="#steam">steam</a>) forms. When liquid water is mentioned in a recipe or cooking method, you can almost always substitute some or all of it with <a href="#stock">stock</a> or another flavorful liquid.</p>
                        </dd>

                        <dt>
                            <a name="whitewash"></a>
                            whitewash
                        </dt>
                        <dd>
                            <p>A whitewash is a <a href="#slurry">slurry</a> made with flour, which is not a pure starch. Some sophisticated palates can detect raw flour flavors in liquids thickened with a whitewash, so roux and purified <a href="#starch">starch</a> slurries are preferred in most professional kitchens.</p>
                        </dd>

                        <dt>
                            wet heat
                        </dt>
                        <dd>
                            <p>See <a href="#moist_heat">moist heat</a>.</p>
                        </dd>

