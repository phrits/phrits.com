                        <dt>
                            <a name="collagen"></a>
                            collagen
                        </dt>
                        <dd>
                            <p>Connective tissue&mdash;the stuff of cartilage, tendons, and sinews that hold an animal's muscles to its bones&mdash;is tough and difficult to eat unless it's properly cooked. With enough time and exposure to <a href="#moist_heat">moist heat</a> at <a href="#simmer">simmering</a> temperatures, the <a href="#protein">proteins</a> in these tissues break down into collagen.</p>
                            <p>Collagen is what gives Jello&reg; its jiggle and pot roast broth its richness and slight stickiness. It contributes depth to <a href="#flavor">flavor</a> as it is very rich in <span class="foreign"><a href="#savory">umami</a></span> or <a href="#savory">savory</a> <a href="#flavor">flavor</a> compounds.</p>
                            <p>Meats that have a lot of connective tissue are generally well-suited to <a href="#braise">braising</a> and often some <a href="#dry_heat">dry heat</a> cooking methods such as <a href="#smoke">smoking</a> or low-temperature <a href="#roast">roasting</a>. A shoulder roast of pork, for example, can be simmered as you might a beef chuck roast. Or you might fix it as <span class="foreign">pernil</span>: Score and cover it in onion and garlic paste (lime juice, salt and pepper, annato if you have it, olive oil to thin) then roast it at 250&deg;F/120&deg;C, turning occasionally, for 4 or 5 hours. Or you might smoke it outdoors with charcoal and hickory for just as long, then chop and mix it with spicy, vinegary Eastern North Carolina Style barbecue sauce.</p>
                        </dd>

                        <dt>
                            <a name="conduction"></a>
                            conduction
                        </dt>
                        <dd>
                            <p>The method of transfering heat by contact. An egg on a griddle is cooked by conduction, and it is conduction that is responsible for the <a href="#carryover">carryover cooking</a> effect.</p>
                        </dd>

                        <dt>
                            <a name="convection"></a>
                            convection
                        </dt>
                        <dd>
                            <p>Convection is a method by which heat in a fluid&mdash;liquid or gas&mdash; moves and disperses through the medium. In an oven, for example, the heating element causes the air very close to it to rise. That air is replaced by other nearby air, and is then itself heated. The result is that the air mixes and the heat distributes. It is convection in a pot of <a href="#simmer">simmering</a> or <a href="#boil">boiling</a> liquid that causes its temperature to be fairly uniform throughout.</p>
                            <p>&quot;Convection&quot; ovens make use of <a href="#mechanical_convection">forced</a> or <a href="#mechanical_convection">mechanical convection</a>, which, while similar, isn't quite the same thing. (Think of blow-drying long hair vs. just letting it air dry.) If it is important in a recipe to distinguish between the two types of oven, the one without the fan is often referred to as &quot;conventional&quot;.</p>
                        </dd>

                        <dt>
                            <a name="custard"></a>
                            custard
                        </dt>
                        <dd>
                            <p>A custard is a mixture of milk, eggs or egg yolks, and other flavorful foods. Examples include the richest &quot;ice creams&quot; and range from cr&egrave;me br&ucirc;l&eacute;e to spinach quiche.</p>
                        </dd>

                        <dt>
                            <span class="glossary_letter"><a name="D_">D</a></span><br />
                            <a name="deep_fat_fry"></a>
                            deep fat fry
                        </dt>
                        <dd>
                            <p>Deep <a href="#fat">fat</a> frying (or simply "deep frying") is a <a href="#dry_heat">dry heat</a> cooking method in which food is submerged in hot, liquid fat, typically in the range of 325&deg;F/160&deg;C to 400&deg;F/200&deg;C depending on the cooking medium's <a href="#smoke_point">smoke point</a>. &quot;Liguid&quot; and &quot;dry&quot; may seem to be incompatible here, but they're not. <a href="#moist_heat">Moist heat</a>, by its very meaning, involves <strong>water</strong> or a water-based liquid.</p>
                            <p>Think about the most common deep fat fried food in America: The French fry. A properly cooked french fry is crisp on the outside, but soft and moist on the inside. The hot oil cooks by heating the potato's contained water well past its boiling point. The <a href="#starch">starch</a> on the inside <a href="#gelatanize">gelatanizes</a> in the hot <a href="#steam">steam</a>, and the outside <a href="#brown">browns</a> and crisps as it would in a hot (and efficient) enough oven. Another piece of that exact same potato cooked in water might be good for mashing or in a salad, but you couldn't politely pick it up with your fingers for dipping in ketchup.</p>
                        </dd>

                        <dt>
                            <a name="denature"></a>
                            denature
                        </dt>
                        <dd>
                            <p>Denaturing is the changing of a protein's molecular shape or structure through cooking or other methods. Cooked meats, pickled fish (such as <span class="foreign">ceviche</span>), and the whipped milk foam with cappuccino are all examples of proteins being denatured.</p
                        </dd>

                        <dt>
                            <a name="dry_heat"></a>
                            dry heat
                        </dt>
                        <dd>
                            <p>Cooking methods generally fall into one of two categories: dry heat or <a href="#moist_heat">moist heat</a>. Dry heat is simply heat in the absence (or near enough) of moisture. Examples include <a href="#roast">roasting</a> or <a href="#bake">baking</a>, <a href="#grill">grilling</a>, <a href="#broil">broiling</a>, <a href="#pan_fry">pan frying</a>, <a href="#smoke">smoking</a>, <a href="#saute">saut&eacute;ing,</a> and even <a href="#deep_fat_fry">deep fat frying</a>.</p>
                        </dd>

                        <dt>
                            <span class="glossary_letter"><a name="E_">E</a></span><br />
                            <a name="emulsify"></a>
                            emulsify
                        </dt>
                        <dd>
                            <p>To emulsify two liquids is to two mutually insoluble liquids into another. Oil and water don't mix, for example, but you can shake Balsamic vinegar, water, and olive oil together, and the mixture will remain stable long enough to dress a salad.</p>
                            <p>Other emulsifications can be made more stable. Egg yolks contain a substance called lecithin, which has a characteristic that's useful in the kitchen: Lecithin molecules are two-ended; one end mixes well with water, the other with oil. With a well-applied whisk, liquid butterfat can be emulsified into water or a wine reduction, becoming a sinfully rich <a href="#hollandaise">Hollandaise</a> sauce.</p>
                        </dd>

                        <dt>
                            espagnole
                        </dt>
                        <dd>
                            <p>See <a href="#brown_sauce">brown sauce</a>.</p>
                        </dd>

                        <dt>
                            <span class="glossary_letter"><a name="F_">F</a></span><br />
                            <a name="fat"></a>
                            fat
                        </dt>
                        <dd>
                            <p>Fat is one of the most important ingredients in cooking. It carries and enhances flavor, it promotes even cooking, it adds texture and richness, and it contributes to a feeling of satiety, the sense of fullness and satisfaction one gets from eating.</p>
                            <p>Fat is necessary for life and it is present in nearly every food source from which we draw our ingredients. It's in whole grains, nuts, and seeds. Meat, eggs, and dairy. It occurs naturally in fruits, and in very high concetrations in some, such as avocados. And of course, it's the main or only ingredient in lard, butter, and shortening. When a fat is liquid at room temperature, it's usually referred to as an <a href="#oil">oil</a>.</p>
                            <p>For nutrition, see <a href="#lipid">lipid</a>.</p>
                        </dd>

                        <dt>
                            <a name="flash_point"></a>
                            flash point
                        </dt>
                        <dd>
                            <p>The flash point is the temperature at which an oil or other hot liquid fat bursts into flame. With few exceptions, fire in a pan is a bad thing. Smother the flame quickly with a lid if you have one that fits. If don't have a lid, you can use salt, baking soda, or a fire extinguisher. (No kitchen should be without one of those.) Using a lid is the only one of those approaches that <strong>might</strong> keep your dish edible. Watch what you're doing and beware of <a href="#smoke_point">smoking oil</a>. You can usually avoid having to choose between your home and your dinner. <strong>Do Not</strong> try to use water to put out a grease fire. It will only make matters worse.</p>
                        </dd>

                        <dt>
                            <a name="flavor"></a>
                            flavor
                        </dt>
                        <dd>
                            <p>Flavor is the attribute of food having to do with taste and smell. Flavors are comprised of sweet, sour, salty, bitter, and <a href="#savory">savory</a> elements, food's smell, and they are apparently affected by picquancy&mdash;hotness, as with a spicy pepper&mdash;and to a lesser extent, a food's texture.</p>
                        </dd>

                        <dt>
                            <a name="flavoring"></a>
                            flavor
                        </dt>
                        <dd>
                            <p>Flavoring or a flavoring agent is isued to alter the <a href="#flavor">flavor</a> of a food. It differs from a <a href="#seasoning">seasoning</a> in that a flavoring agent is intended to bring something new to a dish while a seasoning enhances what is already there. <a href="#mirepoix">Mirepoix</a> and a <a href="#sache">sachet</a> are flavoring agents, as is the cinnamon you might add to a cookie recipe or the garlic, thyme, and oregano you might add to a <a href="#tomato">tomato sauce</a>.</p>
                        </dd>

                        <dt>
                            forced convection
                        </dt>
                        <dd>
                            <p>See <a href="#mechanical_convection">mechanical convection</a>.</p>
                        </dd>

                        <dt>
                            <a name="freezing_point"></a>
                            freezing point or melting point
                        </dt>
                        <dd>
                            <p>The freezing point is the temperature at which a liquid converts to a solid. The melting point is the same exact temperature. Which one to use is only a matter of situational preference. Whether you're using ice water to cool something off or you have water turning to ice in the freezer, the transitional temperature is the same.</p>
                            <p>For pure <a href="#water">water</a>, the temperature is 32&deg;F or 0&deg;C. Adding ingredients will lower that temperature, perhaps significantly. Fruit juice, for example, has a lower freezing point than water, so it wil take longer to solidify in your freezer.</p>
                            <p>The temperature of a freezing liquid is constant; i.e., a well-mixed bowl of ice and water will remain at a constant 32&deg;F/0&deg;C until only the water remains. Salt is sometimes added to lower the temperature&mdash;think of a hand-cranked ice cream churn&mdash;but it may not help as much as folks might hope. You can also salt the ice and water used for <a href="#blanch">blanching</a> to help maintain the flavor. Most of the time, though, that's probably overkill: If you're leaving things in <a href="#shock">shocking</a> water that long, waterlogging is a more significant concern.</p>
                            <p>See also <a href="#boiling_point">boiling point</a>.</p>
                        </dd>

                        <dt>
                            <a name="full_boil"></a>
                            <a name="rolling_boil"></a>
                            full boil or rolling boil
                        </dt>
                        <dd>
                            <p>A full or rolling <a href="#boil">boil</a> is the liquid at its maximum liquid temperature, its <a href="#boiling_point">boiling point</a>. Bubbles of steam grow quickly and rise rapidly, resulting in a vibrant, churning of the liquid.</p>
                        </dd>

                        <dt>
                            <span class="glossary_letter"><a name="G_">G</a></span><br />
                            <a name="gastrique"></a>
                            gastrique
                        </dt>
                        <dd>
                            <p>A gastrique is <a href="#caramelize">caramelized</a> sugar dissolved into vinegar, typically white wine vinegar. It is often flavored with orange or other fruit. The sweet and sour flavors complement each other, and a gastrique can add a great deal of complexity where it's used.</p>
                        </dd>

                        <dt>
                            <a name="gelatanize"></a>
                            gelatanize
                        </dt>
                        <dd>
                            <p>When a <a href="#starch">starch</a> is heated in water, it gelatanizes: The starch molecules swell and soften as they absorb the moisture. Pasta and rice, for example, approximately double in size as they soften, forming new chains and connections in and between the <a href="#carbohydrate">carbohydrate</a> molecules. The same physical process thickens gravy and holds cakes together. See also <a href="#retrograde">retrograde</a>.</p>
                        </dd>

                        <dt>
                            <a name="grill"></a>
                            grill
                        </dt>
                        <dd>
                            <p>Grilling is a <a href="#dry_heat">dry heat</a> cooking method in which food is applied directly to a hot surface, often with just enough fat to prevent sticking. Heat is transferred to the food by <a href="#conduction">conduction</a>.</p>
                            <p>The term &quot;grill&quot; also applies to equipment used for grilling. A grill's surface can be flat and smooth, as a short-order cook might use, or it might have bars or a grid. Grills may be heated by gas, electricity, wood, charcoal, or other suitable fuel.</p>
                        </dd>

                        <dt>
                            <span class="glossary_letter"><a name="H_">H</a></span><br />
                            <a name="hollandaise"></a>
                            Hollandaise
                        </dt>
                        <dd>
                            <p>Hollandaise is one of the five <a href="#mother_sauce">mother sauces</a>. It is clarified butter <a href="#emulsify">emulsified</a> into water or a white wine <a href="#reduce">reduction</a> with an egg yolk emulsifier, then seasoned with salt, lemon juice, and cayenne pepper. If you've ever had Eggs Benedict, you've had Hollandaise.</p>
                            <p>Making a Hollandaise can be tricky, and if its temperature isn't properly managed, it will &quot;break&quot;; i.e., it will separate from a thick, creamy sauce into a gross mess of melted butter and snot-like pieces of egg yolk. A broken sauce can be fixed by starting with another egg yolk and emulsifying the broken mixture into it.</p>
                            <p>Small sauces include Mouseeline, with whipped cream, B&eacute;arnaise with shallots and white wine, and Choron with shallots, white wine, and tomato.</p>
                            <p>Hollandaise is best served on chrome dishes: There's no plate like chrome for the Hollandaise.</p>
                        </dd>

                        <dt>
                            <span class="glossary_letter"><a name="L_">L</a></span><br />
                            leading sauce
                        </dt>
                        <dd>
                            <p>See <a href="#mother_sauce">mother sauce</a>.</p>
                        </dd>

                        <dt>
                            <a name="lipid"></a>
                            lipid
                        </dt>
                        <dd>
                            <p>Lipids are <a href="#fat">fats</a> in food. While cooking approaches will vary when using olive oil or leaf lard, they're digested the same way. Lipids contain almost double the amount of energy by weight&mdash;about 9 Calories per gram&mdash;than <a href="#carbohydrate">carbohydrates</a> or <a href="#protein">proteins</a> (about 5 per gram).</p>
                        </dd>

                        <dt>
                            <span class="glossary_letter"><a name="M_">M</a></span><br />
                            <a name="maillard"></a>
                            Maillard reaction
                        </dt>
                        <dd>
                            <p>The Maillard reaction is the <a href="#brown">browning</a> of amino acids&mdash;chemical chains that make up <a href="#protein">proteins</a>&mdash; and <a href="#sugar">sugars</a> when they're heated together. There are hundreds or even thousands of new chemical compounds that result! It's one of those truly magical things in the kitchen that can transform an ingredient into something wonderful.</p>
                            <p>A boneless, skinless chicken breast <a href="#poach">poached</a> in salted water, for example, will be nutritious. And boring to the point of inedibility. But sprinkle some salt on it instead, and throw it under a broiler for a little while, and you get depth, complexity, flavor, and visual appeal. A plain chicken breast is always going to be boring alone, but the broiled one might bring something tasty to a Caesar salad.</p>
                            <p>The golden brown crust of a whole wheat baguette, the &quot;good part&quot;&mdash;the crusty brown outside&mdash;of a roast prime rib of beef, the way the bread crumbs and that extra sprinkling of cheddar on a potato casserole all toast up together: That's the Maillard reaction at work. Don't forget its friendly cousin, <a href="#caramelize">caremelization</a>, but the <a class="external" title="External link. Opens in new window." target="_blank" href="http://en.wikipedia.org/wiki/Maillard_reaction">chemical reaction Louis Camille Maillard observed</a> is what most often brings that happy conversational silence to a dinner table.</p>
                            <p>Incidentally, <strong>that</strong> is why we <a href="#brown">brown</a> a pot roast before <a href="#braise">braising</a> it, and why we brown hamburger&mdash;drain when it's cooked, then return to the heat and cook until it has some color&mdash;for spaghetti sauce, pork for <span class="foreign">carnitas</span>, or chunks of leftover chicken for a pot pie. It has nothing to do with &quot;sealing in the juices&quot;. If the juices can't get out, the cooking liquid can't get in, and that pot roast's <a href="#collagen">collagen</a> would never break down. Yuck. It's for flavor, pure and simple.</p>
                        </dd>

                        <dt>
                            <a name="mechanical_convection"></a>
                            mechanical convection or forced convection
                        </dt>
                        <dd>
                            <p>Mechanical or forced convection refers to <a  href="#convection">convection</a> heat distribution aided mechanically, typically with one or more rotary fans. Just as convection stirs fluid contents and causes heat to distribute, the fan causes the heat to distribute evenly more quickly. The result is a more efficient cooking environment&mdash;more heat remains in contact with the food&mdash;so cooking temperatures or times are often reduced.</p>
                            <p>Mechanical convection is not suitable for some <a href="#bake">baking</a> or <a href="#roast">roasting</a> applications. A souffl&eacute;, for example, may not be able to rise and stand with all that hot wind blowing around. And a <a href="#custard">custard</a> might develop a film before it had time to set.</p>
                        </dd>

                        <dt>
                            melting point
                        </dt>
                        <dd>
                            <p>See <a href="#freezing_point">freezing point</a>.</p>
                        </dd>

                        <dt>
                            <a name="microwave"></a>
                            microwave
                        </dt>
                        <dd>
                            <p>Microwaves are a type of electromagnetic <a href="#radiation">radiation</a>. The wavelength of microwave energy is close the same size as a water molecule. When water molecules are exposed to microwave radation, they are agitated&mdash;jiggled and bounced around&mdash;producing heat. </p>
                            <p>A microwave oven generates microwaves to cook the food. Because the heat is only generated where the water molecules are, foods can be unevenly heated or cokked. Depending on what's in the oven, you may need to mix or otherwise adjust the food several times during cooking.</p>
                        </dd>

                        <dt>
                            <a name="mirepoix"></a>
                            mirepoix
                        </dt>
                        <dd>
                            <p>Mirepoix (roughly, meer-uh PWAH) is a mixture of ingredients, usually vegetables, cooked together to create a <a href="#flavor">flavor</a> base for a dish or sauce. The &quot;standard&quot; mirepoix is two <a href="#ratio">parts</a> onion and one part each of celery and carrots. If color is important&mdash;say for a very white sauce&mdash;the carrot might be omitted or replaced with parsnips. An Italian <span class="foreign">soffritto</span> is a mirepoix that typically includes garlic. The Cajun &quot;Holy Trinity&quot; is a mirepoix of celery, onion, and green bell pepper.</p>
                        </dd>

                        <dt>
                            <a name="mise_en_place"></a>
                            <span class="foreign">mise en place</span>
                        </dt>
                        <dd>
                            <p>Literally French for &quot;put in place&quot;, a constant sense of <span class="foreign">mise en place</span> is the hallmark of a good cook. Everything from measuring out your ingredients before you start a recipe to figuring out how you're going to <a href="#plate">arrange your finished food</a> on the plate before it's cooked is part of <span class="foreign">mise en place</span>.</p>
                            <p>Anyone can cook. The one who cooks better, though, is the one who's thought it through, who is fully, mentally prepared to be in <strong>that</strong> kitchen performing <strong>that</strong> task toward producing <strong>that</strong> item. It works pretty well as an approach to life, too.</p>
                        </dd>

                        <dt>
                            <a name="moist_heat"></a>
                            <a name="wet_heat"></a>
                            moist heat
                        </dt>
                        <dd>
                            <p>Moist heat cooking involves the prominent presence of water or other water-based liquid. Examples include <a href="#boil">boiling</a>, <a href="#simmer">simmering</a>, <a href="#poach">poaching</a>, <a href="#steam">steaming</a>, and <a href="#braise">braising</a>.</p>
                        </dd>

                        <dt>
                            <a name="mother_sauce"></a>
                            <a name="leading_sauce"></a>
                            mother sauce or leading sauce
                        </dt>
                        <dd>
                            <p>There are five &quot;mother&quot; or &quot;leading&quot; sauces in modern French-influenced cooking. They are so named because they are the bases of so many others. The mother sauces are <a href="#veloute">Velout&eacute;</a>, <a href="#bechamel">B&eacute;chamel</a>, <a href="#espagnole">Espagnole</a> (Brown Sauce), <a href="#tomato">Tomato</a>, and <a href="#hollandaise">Hollandaise</a>. B&eacute;chamel is seldom served as is, for example, but you've eaten it in macaroni &amp; cheese if you've ever had the real thing. Velout&eacute; comes in at least three varieties and can as easily be the base for Cream of Broccoli Soup or Sauce Normandy with oyster juice, cream, and egg yolks.</p>
                            <p>Mnemonic: You can remember the mother sauces with the sentence &quot;Very Bad Eggs Taste Horrible.&quot; (Analytical-Life can't take credit for that: It's taught in the <a href="http://www.google.com/search?hl=en&safe=off&client=firefox-a&hs=34v&rls=org.mozilla%3Aen-US%3Aofficial&q=%2Bcpcc+%2Bculinary+awards&aq=f&aqi=&aql=&oq=&gs_rfai=" class="external" target="_blank" title="Google search +cpcc +culinary awards">top-knotch</a> culinary program at <a href="http://www.cpcc.edu/" class="external">Central Piedmont Community College</a>.)</p>
                            <p>Note that there are sauces that are not descended from these mothers. A simple flavored vinegar doesn't even require heating, for example. Soy Sauce's ancestors and Worcestershire Sauce both begin with the <a href="#ferment">fermentation</a> of anchovies, not <a href="#reduce">reduction</a> of <a href="#stock">stocks</a> or <a href="#emulsify">emulsification</a> of <a href="#fat">fats</a> and liquids. And the only thing <span class="foreign">salsa fresca</span> really has in common with the Tomato mother sauce is the main ingredient.</p>
                        </dd>

                        <dt>
                            <span class="glossary_letter"><a name="N_">N</a></span><br />
                            <a name="napper"></a>
                            <span class="foreign">napper</span>
                        </dt>
                        <dd>
                            <p><span class="foreign">Napper</span> (nap-PAY) refers to a sauce other liquid's ability to coat something. A sauce or <a href="#reduce">reduction</a> has often reached the proper consistency when a spoon dipped in the liquid remains coated&mdash;when it nappes (prounounced "naps") the spoon. You can also draw a the back of a spoon in a line through a thin layer of the liquid&mdash;perhaps a little on a plate&mdash;and the trough will remain.</p>
                        </dd>

                        <dt>
                            <span class="glossary_letter"><a name="O_">O</a></span><br />
                            <a name="oil"></a>
                            oil
                        </dt>
                        <dd>
                            <p>Oil is <a href="#fat">fat</a> that is liquid at room temperature. Oils can be used for cooking in <a href="#deep_fat_fry">deep fat</a> and <a href="#pan_fry">pan frying</a>. Many oils&mdash;e.g., extra virgin olive oil, sesame, walnut, orange, avocado&mdash;are most often eaten raw or in dressings. Others&mdash;e.g., peanut, safflower, canola&mdash;are most often used for cooking. As with other <a href="#lipid">lipids</a>, oils contain about 9 nutritional calories (kCal) per gram.</p>
                            <p>When speaking of hot fat, as that for deep frying, it is often referred to as &quot;oil&quot; regardless of whether it is liquid at room temperature. Butter is always butter, though, and it deserves to be.</p>
                        </dd>

                        <dt>
                            <span class="glossary_letter"><a name="P_">P</a></span><br />
                            <a name="pan_fry"></a>
                            pan fry
                        </dt>
                        <dd>
                            <p>Pan frying is pretty much what it sounds like: frying food in a pan. For pan frying, the fat should be at a depth so that it's about 1/3 to 1/2 the height of the food in the pan. It's a method that works best for foods that are relatively flat: chicken pieces, breaded veal cutlest for <span class="foreign">wiener schnitzel</span>, etc.</p>
                            <p>See also <a href="#saute">saut&eacute;</a>.</p>
                        </dd>

                        <dt>
                            <a name="plate"></a>
                            plate
                        </dt>
                        <dd>
                            <p>Plating is the practice of putting food on a plate in an attractive manner. Much of cooking is a balance of craft and science; plating is pure art.</p>
                        </dd>

                        <dt>
                            <a name="poach"></a>
                            poach
                        </dt>
                        <dd>
                            <p>Poaching is a <a href="#moist_heat">moist heat</a> cooking method. To poach a food is to cook it in very still but very hot liquid, typically 165&deg;F/75&deg;C to 185&deg;F/85&deg;C. It is best used for delicate foods that need gentle heating such as shelled eggs for Benedict or a fish filet you'd like to infuse with wine.</p>
                            <p>See also <a href="#boil">boil</a> and <a href="#simmer">simmer</a>.</p>
                        </dd>

                        <dt>
                            <a name="protein"></a>
                            protein
                        </dt>
                        <dd>
                            <p>Generically speaking, protein is the item in a full meal from which one receives nutritional protein. It's the chicken breast or steak, the salmon under the mousseline, or the absence of animal proteins, it's the beans that go with the rice.</p>
                            <p>Chemically, proteins are made up of other elements common to life&mdash;Carbon, Hydrogen, Oxygen&mdash;but they also contain Nitrogen and other elements including Sulfur or Iron. Protein molecules are characterized by complex arrangements, knotted and twisted in nearly infinite ways. Exposed to heat, proteins either <a href="#coagulate">coagulate</a>&mdash;clump together&mdash;or <a href="#denature">denature</a>&mdash;unwind, and break down into smaller chains.</p>
                            <p>In the kitchen, it is proteins that give us the <a href="#maillard">Maillard reaction</a>. It is proteins in connective tissue that break down into <a href="#collagen">collagen</a> proteins, producing rich, hearty flavors in <a href="stock">stocks</a>, sauces, stews, and pot roasts. The albumen that changes from clear to white in shrimp or egg white as they cook is protein.</p>
                        </dd>

                        <dt>
                            <span class="glossary_letter"><a name="R_">R</a></span><br />
                            <a name="radiation"></a>
                            radiation
                        </dt>
                        <dd>
                            <p>Radiation is the transmission of heat that occurs even in the absence of contact (<a href="#conduction">conduction</a>) or a fluid medium such as water or air (<a href="#convection">convection</a>). When food is <a href="#grill">grilled</a> over charcoal, and even more clearly when it is <a href="#broil">broiled</a> from above, the bulk of the heat is from radiation. In space, no one can hear you scream; but your electric broiler can still cook your steak.</p>
                            <p>Heat is transmitted in the form electromagnetic waves, which is the same kind of energy as that from a flashlight, your stereo speakers, your cell phone, or a <a href="#microwave">microwave oven</a>. The main difference among those usages is the wavelength, or if you prefer, the wave's frequency. For more than this author groks beyond that, <a href="http://en.wikipedia.org/wiki/Thermal_radiation" title="External Link. Opens in a separate window." class="external">Wikipedia's article on Heat</a> is a good place to start.</p>
                        </dd>

                        <dt>
                            <a name="ratio"></a>
                            ratio
                        </dt>
                        <dd>
                            <p>A ratio is a proportion, for example &quot;two to one&quot;, or 2:1 or 2-1 as that might be written, sometimes as percentages 100/50. It means that for every two parts of an ingredient&mdash;two ounces or two cups, for example&mdash;there will be one part&mdash;one ounce or cup&mdash; of another.</p>
                            <p>For culinary purposes, ratios are described in parts by weight&mdash;ounces and pounds, grams and kilograms&mdash;or by volume&mdash;tablespoons, cups, gallons; milliliters (mL), liters (L). Weight and volume are almost never used together. For water and liquids very much like water, weights and volumes are essentially interchangeable, but do be careful: Oil floats; a pint weighs less than a pound.</p>
                            <p><a href="#mirepoix">Mirepoix</a> is 50/25/25 Onions/Carrots/Celery by weight. With the ratio 100/60/3/2 Flour/Water/Salt/Yeast, (again by weight) and a little imagination, you can bake bread every day of the week for a year, never have the same thing twice, and never open a cookbook. A good scale is worth the investment. So is the book, <i>Ratio</i> by Michael Ruhlman, which should be available at your favorite bookseller.</p>
                        </dd>

                        <dt>
                            <a name="reduce"></a>
                            reduce
                        </dt>
                        <dd>
                            <p>To reduce a liquid&mdash;wine or stock for a sauce, for example&mdash;is to <a href="#simmer">simmer</a> it to cause evaporation of water. As the water evaporates, the liquid gets more concentrated, and carried far enough it will begin to thicken as well. When a liquid is reduced to the point that it <a href="#napper">nappes</a> a spoon and will be served as a sauce, it is called a &quot;reduction&quot;.</p>
                            <p>Directions to reduce a liquid often state how much to do so. To reduce a stock by half or 50%, for example, is to simmer it until it is only half the volume it was when it started. A quart reduced to a pint is reduced by half. That same quart reduced to a cup is reduced by three quarters, or 75%. You might also see a recipe calling for something to be reduced <span class="foreign">au sec</span>, which means to reduce it until it is almost dry.</p>
                        </dd>

                        <dt>
                            <a name="retrograde"></a>
                            retrograde
                        </dt>
                        <dd>
                            <p>When a <a href="#starch">starch</a> retrogrades, it releases some of its held moisture, and it shrinks. Retrogradation is essentially the undoing of <a href="#gelatanize">geletanization</a>. Staling bread, rice that shrinks back to itself just enough to be fluffy, the way a cake pulls away from the pan a little bit as it cools: Retrograding starches.</p>
                        </dd>

