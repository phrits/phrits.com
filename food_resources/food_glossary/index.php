                    <dl>
                        <dt>
                            <span class="glossary_letter"><a name="A_">A</a></span><br />
                            <a name="a_la_minute"></a>
                            <span class="foreign">&agrave; la minute</span>
                        </dt>
                        <dd>
                            <p>Literally French for &quot;to the minute&quot;. Dishes prepared precisely when needed, such as for custom orders or tableside presentations. Steak Diane, Guacamole, Caesar Salad, and Bananas Foster are all traditionally prepared tableside <span class="foreign">&agrave; la minute</span>.</p>
                        </dd>

                        <dt>
                        <dt>
                            <a name="al_dente"></a>
                            <span class="foreign">al dente</span>
                        </dt>
                        <dd>
                            <p>Literally Italian for &quot;to the tooth&quot;. The slight firmness of properly cooked pasta. Overcooked pasta is mushy and generally too weak to hold together with sauces or fillings.</p>
                        </dd>

                        <dt>
                            <span class="glossary_letter"><a name="B_">B</a></span><br />
                            <a name="bake"></a>
                            bake
                        </dt>
                        <dd>
                            <p>Baking is a <a href="#dry_heat">dry heat</a> cooking method in which an item is placed in an enclosed, heated container such as an oven. Heat is transferred by <a href="#convection">convection</a> and <a href="#radiation">radiation</a>. Baking is really the same as <a href="#roast">roasting</a>, the difference in usage mostly referring to what's being cooked. Breads and other, well, bakery items, are typically &quot;baked&quot;, while a ham or prime rib of beef would instead be &quot;roasted&quot; Baked chicken? Sure. Baked Potatoes? Yup. Baked beef? Not technically wrong, but certainly not a common use of the word.</p>
                            <p>There's also a such thing as a steam oven, used in professional bakeries. They don't cook with enough <a href="#steam">steam</a> to be considered a <a href="#moist_heat">moist heat</a> cooking method, but the extra moisture in the air causes the <a href="#starch">starch</a> in the flour to <a href="#gelatanize">gelatanize</a>, and produces that wonderful crisp baguette crust. If you listen closely, you can hear a good crust crackling as it cools.</p>
                        </dd>

                        <dt>
                            <a name="bechamel"></a>
                            B&eacute;chamel
                        </dt>
                        <dd>
                            <p>Sauce B&eacute;chamel is one of the <a href="#mother_sauce">mother sauces</a>. It is milk, mildly <a href="#flavor">flavored</a> with onion, nutmeg, and clove; <a href="#reduce">reduced</a>, and <a href="#thicken">thickened</a> with a white or blonde <a href="#roux">roux</a>. <a href="#small_sauce">Small sauces</a> include Mornay (Swiss cheese) and Supr&ecirc;me (enriched with cream).</p>
                        </dd>

                        <dt>
                            <a name="blanch"></a>
                            blanch
                        </dt>
                        <dd>
                            <p>To partially cook something, usually to reduce later cooking time or to preserve an ingredient's color. Food can be blanched in <a href="#water">water</a>-based liquids as one might do with green beans to be <a href="/cooking_school/glossary/#saute">saut&eacute;ed</a> later. Food can also be blanched in <a href="#oil">oil</a> or deep <a href="#fat">fat</a>, such as one might do with a breaded <span class="foreign">chicken cordon bleu</span> for browning before finishing it in a hot oven just before <a href="#plate">plating</a>. See also <a href="#shock">shock</a>.</p>
                            <p>Since blanching can be done with either water or oil, it is typically considered to be neither a <a href="#dry_heat">dry heat</a> nor a <a href="moist_heat">moist heat</a> cooking method.</p>
                        </dd>

                        <dt>
                            <a name="boil"></a>
                            boil
                        </dt>
                        <dd>
                            <p>Boiling is a <a href="moist_heat">moist heat</a> cooking method in which food is submerged in <a href="#water">water</a> at its maximum liquid temperature, its <a href="#boiling_point">boiling point</a>. Boiling is characterized by rapid movement and agitation, so it is best suited for foods that benefit from the activity. Pasta boils well, but <a href="#stock">stocks</a> become cloudy, and biscuit-style dumplings completely fall apart. The term is often misapplied: &quot;Hard-boiled&quot; eggs, for example are more often <a href="#simmer">simmered</a>.</p>
                            <p>See also <a href="#poach">poach</a>.</p>
                        </dd>

                        <dt>
                            <a name="boiling_point"></a>
                            boiling point
                        </dt>
                        <dd>
                            <p>The temperature at which a liquid converts to a gas. For pure <a href="#water">water</a> at sea level, the temperature is 212&deg;F or 100&deg;C. At higher elevations, because of the naturally lower air pressure, liquids boil at lower temperatures. Adding ingredients will raise the temperature slightly. In neither case, however, are cooking times significantly affected. We salt pasta waster for flavor: The heat increase is tiny and the difference in time is well below what you'd measure.</p>
                            <p>The temperature of a pure boiling liquid is constant; i.e., pure water at sea level at a full boil is going to remain at a constant 100&deg;C. <a href="#steam">Steam</a> itself&mdash;the invisible stuff in the bubbles, not the stuff you see condensing in the air over the pot&mdash;can be much hotter. In physics or chemistry, you might also see the term &quot;condensation point&quot; which is interchangable with &quot;boiling point&quot;, but you'll probably never see the former used in a recipe.</p>
                            <p>See also <a href="#freezing_point">freezing point</a>.</p>
                        </dd>

                        <dt>
                            <a name="braise"></a>
                            braise
                        </dt>
                        <dd>
                            <p>Braising is a <a href="#moist_heat">moist heat</a> cooking method involving enough <a href="#water">water</a> to at least partially cover the food being cooked, long cooking times, and relatively low temperatures. It's sometimes described as a &quot;hybrid&quot; method because a food is often <a href="#brown">browned</a>&mdash;typically saut&eacute;ed&mdash;for extra <a href="#flavor">flavor</a> and color before putting it in the liquid for full cooking. While many foods are considered sufficiently cooked when they reach a particular internal temperature, braised foods are usually cooked until they reach the desired level of tenderness. The method is best applied to tough cuts of meat that have a high proportion of connective tissue, which turns into <a href="#collagen">collagen</a>. The classic American pot roast is a perfect example of a cut best braised.</p>
                        </dd>

                        <dt>
                            <a name="broil"></a>
                            broil
                        </dt>
                        <dd>
                            <p>Broiling is a <a href="#dry_heat">dry heat</a> cooking method characterized by high temperature heat transferred from above by <a href="#radiation">radiation</a>. Results are similar to <a href="#grill">grilling</a>, and the two methods are often interchangable. Broiling is often used in professional and home kitchens to <a href="#brown">brown</a> the top of a food just before service.</p>
                        </dd>

                        <dt>
                            <a name="brown"></a>
                            brown
                        </dt>
                        <dd>
                            <p>Browning typically describes one of two chemical reactions. Sometimes it refers to <a href="#caramelize">caramelization</a> the chemical changes of <a href="#sugar">sugars</a> and <a href="#starch">starches</a>, usually under <a href="#dry_heat">dry heat</a>. More often, though, the term refers to the browning of meat, the <a href="#maillard">Maillard reaction</a>. That browning is a complex interaction of the amino acids that make up <a href="#protein">proteins</a>, and <a href="#sugar">sugars</a>. In both cases, the browning adds <a href="#flavor">flavor</a> and color to the food being cooked.</p>
                        </dd>

                        <dt>
                            <a name="brown_sauce"></a>
                            <a name="espagnole"></a>
                            brown sauce or espagnole
                        </dt>
                        <dd>
                            <p>Brown Sauce or Sauce Espagnole is one of the <a href="#mother_sauce">mother sauces</a>. It is brown <a href="#stock">stock</a>, <a href="#reduce">reduced</a> a little while its <a href="#flavor">flavor</a> is boosted with a fresh round of <a href="#caramelize">caramelized</a> <a href="#mirepoix">mirepoix</a> and a <a href="#sachet">sachet</a>, then thickened with a brown <a href="#roux">roux</a>.</p>
                            <p>Brown sauce is often reduced further into a demi-glace: Start with a quart of brown sauce, reduce it by half, add a quart of brown stock, and reduce it by half again.</p>
                            <p><a href="#small_sauce">Small sauces</a> include Sauce Robert, with butter, onion, white wine and mustard; Diable, with white wine, shallots, and peppercorns; and Piquante, with shallots, vinegar, wine, capers, pickles, parsley, and tarragon.</p>
                            <p>To be technically correct, there may be minor differences between &quot;brown sauce&quot; and its French term, &quot;espagnole&quot; Those differences, if they exist, may have to do with whether or how much tomato product is introduced to the mix. For our purposes at <a href="http://analytical-life.com/">Analytical-Life</a>, the two sauces are interchangeable.</p>
                        </dd>

                        <dt>
                            <span class="glossary_letter"><a name="C_">C</a></span><br />
                            <a name="carbohydrate"></a>
                            carbohydrate
                        </dt>
                        <dd>
                            <p>Carbohydrates are one of three major (macro-) nutrients needed to sustain life, the other two being <a href="#fat">fats</a> and <a href="#protein">protein.</a>. <a href="#sugar">Sugars</a>, <a href="#starch">starches</a>, and fiber are all carbohydrates.</p>
                            <p>Nutritionally, carbohydrates contain about the same amount of energy as proteins, roughly 5 kCal per gram.</p>
                        </dd>

                        <dt>
                            <a name="caramelize"></a>
                            caramelize
                        </dt>
                        <dd>
                            <p>Caramelization is the <a href="#brown">browning</a> reaction of <a href="#sugar">sugars</a> and <a href="#starch">starches</a>, usually when exposed to <a href="#dry_heat">dry heat</a>. When a <a href="#mirepoix">mirepoix</a> is headed for a brown stock, you'll generally <a href="#saute">saut&eacute;</a> it, often with a little tomato paste, until it starts to brown. You can melt sugar and heat it until it starts to turn slighly brown. Add some cream to that and you have a simple caramel candy. Caramelization carried too far leaves nothing but black, bitter carbon, the hydrogen and oxygen having been driven off into the air.</p>
                        </dd>

                        <dt>
                            <a name="carryover"></a>
                            <a name="carryover_cooking"></a>
                            carryover cooking
                        </dt>
                        <dd>
                            <p>When you remove cooking food from heat&mdash;for example, when you take a roast from the oven&mdash;it is still hot. The heat that the food itself contains will cause it to continue to cook itself a little bit. The &quot;magic&quot; in cooking something to perfection is a matter of knowing just exactly how much more something will cook, and allowing for it. In the case of the example roast, you might take it out of the oven at an internal temperature of 145&deg;F, knowing that it will probably reach about 150&deg;F, which might be closer to your particular medium-well preference.</p>
                            <p>Alternatively, you might <a href="#shock">shock</a> some <a href="#blanch">blanched</a> vegetables in ice water, stopping their cooking just exactly where they are at that moment.</p>
                        </dd>

