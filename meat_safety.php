            <p><a href="http://www.fda.gov/Food/ResourcesForYou/HealthEducators/ucm082294.htm">Here</a> is a good place to start, and <a href=http://adventure.howstuffworks.com/survival/wilderness/eating-raw-meat1.htm">this</a> might also help.</p>

            <p>There are a couple of things going on. First is the type of biological hazard. Poultry and reptiles (e.g., alligator) tend to carry salmonella, so that's the most likely threat from raw chicken and raw eggs. Healthy cattle don't carry much of anything&mdash;at least in fat and the parts that become meat&mdash;but modern processing methods put the edible parts in contact with the digestive tract that carries, for example, e. coli.</p>

            <p>Pork processing is similar to beef, and a rare pork chop is fine. We USians are culturally afraid of rare pork, though, because of trichinosis, a threat that's essentially been eliminated in the pork available to the public.</p>

            <p>Raw eggs are generally safe for healthy people who don't have a compromised immune system, but they're not recommended for the very young, very old, or otherwise at risk folks including HIV patients, etc. Eggs are washed before being sold and what little bit of salmonella that might still be inside presents only a tiny danger to most of us.</p>

            <p>Germs tend to live on the surface of cut meat, so a properly handled and cooked steak&mdash;even very rare&mdash;is no threat: The whole surface has been sanitized by the heat of cooking. Dry aging takes place in controlled conditions, and the surface is cooked before serving.</p>

            <p>If you run that same raw steak through a grinder, though, you've mixed the surface bacteria in with the rest of it. A hamburger has an essentially infinite surface in that regard, so it should be cooked sufficiently to sanitize the inside as well. I don't think dry-aged beef is ever used for grinding.</p>

            <p>The raw fish used for sushi is carefully processed to avoid the sort of contaminants that beef processing introduces. Beef used for carpaccio is clean and handled carefully.</p>

            <p>Cured meats are often safe &quot;raw&quot;&mdash;country ham comes immediately to mind&mdash;but it's not a safe assumption for, say, grocery store bacon or packaged slices of country ham. Anything labeled &quot;fully cooked&quot; and properly stored is safe, including most hot dogs.</p>

            <p>Some things are just better cooked. Hot dogs, certainly, and I suspect pork falls into that category.</p>

            <p>Even with all of that, most of what you can pick up at the grocery is likely to be safe. Yes, <a href="http://www.frictionmagazine.com/imprint/books/fastfood_print.html">there's shit on the beef</a>, but it has usually been sprayed with sanitizing chemicals and/or irradiated to kill the germs and remove the toxins. A bite of raw beef, even hamburger, is likely to cause a healthy person no harm.</p>

            <p>Still, better safe than sorry in most cases, and that's the approach the FDA standards use. For my part, I make my mayonnaise with raw eggs&mdash;unless I'm feeding someone from an at risk group&mdash;I've been known to eat a few bites of raw beef tenderloin, and I could probably eat my weight in uncooked country ham.</p>

            <p>I've been rather casual with my language above, by the way. Salmonella, for example, is a bacteria that causes salmonellosis, while trichinosis is the actual infection *caused by* the roundworm (trichinella spiralis) that causes it. By &quot;germ&quot; I mean any sort of biological hazard.</p>

