            <h1>Tomato Sauce</h1>
            <h2>Ingredients</h2>
            <ul>
                <li class="ingredient">1/4 cup extra virgin olive oil</li>
                <li class="ingredient">1 large onion, finely chopped</li>
                <li class="ingredient">2 carrots, finely chopped</li>
                <li class="ingredient">2 stalks of celery, finely chopped</li>
                <li class="ingredient">5 cloves of garlic, finely chopped</li>
                <li class="ingredient">1 small bell pepper, finely chopped</li>
                <li class="ingredient">3 15-ounce cans of tomatoes</li>
                <li class="ingredient">2 Tablespoons dried thyme</li>
                <li class="ingredient">1 Tablespoon dried oregano</li>
                <li class="ingredient">3 dried bay leaves</li>
                <li class="ingredient">2 Tablespoons sugar</li>
                <li class="ingredient">Salt and pepper to taste</li>
            </ul>
            <h2>Instructions</h2>
            <ol>
                <li>Sweat the vegetables in the oil until the onions are transluscent.</li>
                <li>Add the tomatoes, thyme, oregano, bay, and sugar.</li>
                <li>Bring to a boil, then immediately reduce to a simmer.</li>
                <li>Cook for half an hour to 45 minutes.</li>
                <li>(Optional) Blend or put the sauce through a food mill until the sauce is smooth and uniform rather than chunky.</li>
                <li>Add salt and pepper to taste.</li>
            </ol>
            <h2>Yield</h2>
            <p>Makes about 2 quarts.</p>

            <h2>Variations</h2>
            <p>If you're looking for an &quot;Italian&quot; sauce, use green bell pepper instead of celery. For a really nice chili base, trade the celery for poblanos and jalape&ntilde;os.</p>

