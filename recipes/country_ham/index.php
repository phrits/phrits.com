            <h1>Country Ham</h1>
            <p>Country Ham, sometimes called Virginia or Smithfield Ham, is dry-cured for several months. It's similar to Prosciutto rather than to insipid spiral-sliced, canned, or other wet-cured hams. Real Country Ham is edible raw, a result of the long drying and curing process. Go ahead and taste it. You know you want to.</p>
            <p>Because it's so salty, a Country Ham will go farther than another roast of the same size. You may want to cook something smaller for your table. Your butcher might sell you half a ham or should at least be happy to cut a whole one in two for you. If you're having that done anyway, go ahead and have the hock cut off too.</p>
            <h2>Ingredients</h2>
            <ul>
                <li class="ingredient">One whole country ham</li>
                <li class="ingredient">Four to eight cups of coffe. Leftover is fine.</li>
            </ul>
            <h2>Preparation</h2>
            <ol>
                <li>Whole hams are usually wrapped in paper then put into a cloth bag. Remove and discard the paper and cloth.</li>
                <li>Use a stiff brush and running water to remove any visible mold.</li>
                <li>With a bone saw&mdash;a clean pruning saw works nicely&mdash;remove the hock, the 4 to 6 inches at the small end that's more gristle and bone than meat. You can probably get your butcher to do this part for you. You don't need the hock for the roast, but do keep it for another use such as the best pot of beans you've ever had.</li>
                <li>(Optional) In a very large container, cover the ham with water. Let it soak for one to three days, changing the water every day.<br />
                <span class="emph">Note:</span> I haven't soaked a ham in years. A country ham's intense saltiness is part of its charm. Most recipes recommend soaking, though, so I've included the step for the timid, Yankees, and other folks who just don't know any better.</li>
            </ol>
            <h2>Instructions</h2>
            <ol>
                <li>Heat the oven to 300&deg;F/150&deg;C.</li>
                <li>Place the ham on a rack in a deep roasting pan.</li>
                <li>Add the coffee to the bottom of the pan.</li>
                <li>Cover the pan with a lid or heavy duty aluminum foil.</li>
                <li>Cook for about 20 minutes per pound. The cooking here is more of a braise than a roast, and you're more after a tender texture than you are trying to hit a perfect medium-rare internal temperature.</li>
                <li>Remove the ham to a platter and allow it to rest before carving.</li>
                <li>Transfer the liquid from the pan to a fat separator or a clean saucepot. Remove most of the fat. Taste and simmer with a little water if it's too potent. There's your gravy.</li>
                <li>Serve with your favorite sides.</li>
            </ol>
            <h2>Red-eye Gravy</h2>
            <p>Red-eye gravy isn't a traditional gravy: It's really just coffee flavored with ham drippings, not thickened at all. If your gravy is too salty, add some water and simmer briefly. If the coffee bitterness is too much, just add a little water and simmer some ham in it for ten minutes or so.</p>
            <h2>Sides</h2>
            <p>A Southern tradition such as whole country ham calls practically demands great side dishes: Homemade biscuits and apple butter, pintos or slow-cooked green beans, mashed potatoes, or anything else that's good with gravy.</p>
            <h2>Leftovers</h2>
            <p>Small slices of leftover ham are fantastic on biscuits. Add scraps to soups or sauces. Ham hash is as good or better than corned beef. Simmer the hock for a couple of hours in a pot of beans, then shred the meat into the dish (or just "taste" it away) before serving.</p>
            <h2>Variation: Glazed Ham</h2>
            <p>You can glaze a Country Ham as you might any other, but be sure to pull the red-eye gravy out of the pan first.</p>
            <h3>Ingredients</h3>
            <ul>
                <li class="ingredient">One whole country ham, roasted, fresh from the oven</li>
                <li class="ingredient">1/2 cup brown sugar</li>
                <li class="ingredient">1/4 cup yellow mustard</li>
                <li class="ingredient">2 Tablespoons orange juice</li>
            </ul>
            <h3>Instructions</h3>
            <ol>
                <li>Mix together the sugar, mustard, and juice.</li>
                <li>Drizzle over the ham.</li>
                <li>Return ham to the oven and roast uncovered for ten to fifteen minutes.</li>
                <li>Rest and carve as usual.</li>
            </ol>
            <h2>Variation: Slices</h2>
            <p>This is a good use for your electric skillet if you happen to have one.</p>
            <h3>Ingredients</h3>
            <ul>
                <li class="ingredient">Sliced country ham</li>
                <li class="ingredient">A cup or two of brewed coffee. Leftover is fine.</li>
                <li class="ingredient">Water as needed</li>
            </ul>
            <h3>Instructions</h3>
            <ol>
                <li>Trim any excess fat from the slices. Don't be too careful to get it all.</li>
                <li>Over medium high heat, fry the scraps until they release some of the fat. You can continue to cook the scraps until they become crisp and brown. You don't have to share those: Cook's Privilege.</li>
                <li>Fry each slice until it starts to develop a little bit of crust, then remove it from the pan while you brown the others.</li>
                <li>Add the slices back to the pan and cover with coffee</li>
                <li>Bring to a boil, then reduce to a simmer.</li>
                <li>Simmer until the slices are tender and the coffee has lost its bitterness.</li>
                <li>Add some water if the gravy is stronger than you like. Be sure to simmer it for a few minutes afterward, though, so the gravy doesn't taste watery.</li>
                <li>Serve with your favorite sides.</li>
            </ol>
