            <h1>Stuffed Shells</h1>
            <p></p>
            <h2>Ingredients</h2>
            <ul>
                <li class="ingredient">1 1/2 pounds ground beef</li>
                <li class="ingredient">1 pound ground pork</li>
                <li class="ingredient">1 large onion, finely chopped</li>
                <li class="ingredient">5 cloves garlic, minced</li>
                <li class="ingredient">3/4 pound parmesan cheese, grated</li>
                <li class="ingredient">1 pound mozzarella cheese, grated</li>
                <li class="ingredient">24 ounces ricotta cheese</li>
                <li class="ingredient">1 cup bread crumbs</li>
                <li class="ingredient">1 cup heavy cream (optional)</li>
                <li class="ingredient">3 large eggs, lightly beaten</li>
                <li class="ingredient">3 tablespoons dried thyme</li>
                <li class="ingredient">2 tablespoons dried oregano</li>
                <li class="ingredient">3 pounds frozen spinach, cooked and squeezed dry (optional)</li>
                <li class="ingredient">1/4 cup fresh basil</li>
                <li class="ingredient">salt and pepper to taste</li>
                <li class="ingredient">24 ounces (2 boxes) jumbo pasta shells</li>
                <li class="ingredient">2 to 4 batches of <a href="/recipes/tomato_sauce.php">tomato sauce</a>
            </ul>
            <h2>Instructions</h2>
            <ol>
                <li>If you're using the heavy cream, soak the bread crumbs in it.</li>
                <li>Brown the beef and the pork together. Drain, set aside, and let cool.</li>
                <li>Set aside about 1/3 of the parmesan and mozzarella for putting on top of the shells during baking.</li>
                <li>In a large bowl, combine the meat, onion, garlic, 2/3 of the grated cheese, ricotta cheese, spinach, eggs, bread crumbs, herbs, and salt and pepper.</li>
                <li>Cook the shells, being careful not to break them and to leave them a little bit undercooked. Drain, cool slightly, and place them individually on a large sheet or two of waxed paper. If you let them cool too much, it will be hard to separate them without tearing them. Medical latex gloves, if you happen to have any on hand for handling hot peppers or whatever, may provide just enough heat protection to allow you to move the shells to the waxed paper.</li>
                <li>Fill the shells with about a 1/4 cup each of the meat mixture. If you run out of shells before you run out of mixture, freeze the remaining mixture or bake it and serve as an Italian meat loaf. If you run out of mixture first, discard the remaining shells or eat them as a snack. With a little practice, you'll probably get almost exactly 6 dozen stuffed shells out of this recipe.</li>
                <li>Heat an oven to 350&deg;F/175&deg;C.</li>
                <li>Cover the bottom of a large baking pan with tomato sauce. Place the shells in the pan and pour more sauce over the top. You should be able to fit about 36 shells into a good-sized pan, in which case you'll need about one jar for the bottom and two on top.</li>
                <li>Bake for 30 - 40 minutes until hot and bubbly. Cover with half the remaining cheese (all of it if you managed to fit them all into one pan) and return to the oven until the cheese is melted and slightly browned&mdash;about 15 to 20 minutes.</li>
            </ol>
            <h2>Yield</h2>
            <p>About 6 dozen shells, enough to feed 20 to 24.</p>
            <p>Shells can be made ahead of time and frozen with or without the tomato sauce. They do not need to be thawed before cooking. This recipe can be cut in half, but it takes enough time that I've always just made more than I need and frozen the rest.</p>
