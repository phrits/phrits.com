            <h1 class="inline">Guacamole</h1><div class="inline">
            <p>My take on guacamole: Chunky and with a bite.</p>
            <h2>Ingredients</h2>
            <ul>
                <li class="ingredient">4 ripe avocados, pitted, peeled, and cut into 3/4-inch chunks</li>
                <li class="ingredient">1 small red onion, finely chopped</li>
                <li class="ingredient">2 serrano peppers, seeded (if you must), and finely chopped</li>
                <li class="ingredient">3 cloves garlic, minced</li>
                <li class="ingredient">1 large tomato, seeded and cut into 1/2-inch pieces</li>
                <li class="ingredient">1/4 cup coarsely chopped fresh cilantro</li>
                <li class="ingredient">1 to 2 teaspoons ground cumin (cilantro seed)</li>
                <li class="ingredient">salt and pepper to taste</li>
                <li class="ingredient">lime juice to taste</li>
            </ul>
            <h2>Instructions</h2>
            <ol>
                <li>Combine the onions, peppers, cumin, and cilantro in a large bowl.</li>
                <li>Add the avocados and mix gently.</li>
                <li>Add salt, pepper, and lime juice to taste and mix gently.</li>
                <li>Serve with tortilla chips. You can also put some plastic wrap directly on it, then cover and refrigerate for later.</li>
            </ol>
            <h2>Yield</h2>
            <p>Serves at least 8.</p>
            <h2>Notes</h2>
            <p>Most guacamole has you mashing avocados, and then it turns brown quicly. This salad isn't quite as sensitive.</p>
            <h2>Updated</h2>
