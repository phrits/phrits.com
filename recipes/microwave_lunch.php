            <h1>Healthy Office Lunch</h1>
            <p>I've been in the habit over the last year or so of making my lunch at work. It's inexpensive, healthful, and tastes fresher than everyday dinner leftovers. My base recipe is chicken and spinach, but see the Variations for other ideas.</p>
            <h2>Ingredients</h2>
            <ul>
                <li class="ingredient">4 oz. cooked chicken</li>
                <li class="ingredient">Italian seasoning, crushed red pepper, and garlic powder to taste</li>
                <li class="ingredient">8 oz. frozen spinach</li>
                <li class="ingredient">salt, pepper, and garlic powder to taste</li>
            </ul>
            <h2>Instructions</h2>
            <ol>
                <li>Break or cut the chicken into small bite-sized pieces. Add as much Italian seasoning, crushed red pepper, and garlic powder as you prefer and mix well. Set aside.</li>
                <li>In a microwave-safe container, add as much salt, pepper, and and garlic powder to the spinach as you prefer. It's okay if the spinach has thawed: Just knock 15-30 seconds off the final cook time.</li>
                <li>Cover loosely, but make sure it's vented. Microwave on high for 2 minutes. Stir. Microwave for another 1 to 3 minutes depending on the oven wattage and how tender you like your spinach.</li>
                <li>Remove cover and add chicken. Cover and let stand for another minute before eating.</li>
            </ol>
            <h2>Yield</h2>
            <p>Serves one, but it's a pretty light meal.</p>
            <h2>Preparation Notes</h2>
                <p>I like to roast a couple of chickens, then divide the meat into 4-ounce portions before freezing. The container or plastic bag is perfect for getting it well-mixed with the seasonings.</p>
                <p>Chicken can taste a little off when it's reheated, and it's pretty easy to ruin leftover steak or pork by cooking it too long. I prefer to let the heat of the dish take any remaining chill off the protein. If you're using something that doesn't suffer from a second cooking--bacon or bratwurst, for example--you may prefer it heated back up to temperature.</p>
                <p>I carry my spinach (or other vegetables, below) in the plastic container I cook it in, and I include any accents--sliced mushrooms or minced fresh garlic, for example, or a splash of heavy cream--with it. I wait to season until cook time.</p>
            <h2>Variations</h2>
                <p>The approach described above works for a wide variety of ingredients. You can adapt it to your own tastes. Whatever you like!
                <p>Switch out the vegetable base. You can use frozen broccoli, cauliflower, or a mix, but cook for not quite as long. You can use kale, collards, etc., but remember to cook tougher greens longer. Peas and carrots. Green beans. Lima beans. Stir-fry mix. Brussels sprouts. Cabbage.</p>
                <p>Add fresh, cooked, or frozen accent vegetables: Bell peppers, jalape√±os, serranos. Onions, garlic, shallots. Mushrooms. Sauerkraut. Tomatoes.</p>
                <p>Use a different protein, or omit it entirely. A diced pork chop or leftover steak works nicely. Crumbled cooked hamburger or ground turkey. Sausages. Tofu. Canned tuna. You can chop up some cooked bacon and crisp it in with the spinach while that cooks to quite good effect.</p>
                <p>Change the flavorings or style. Instead of salt and Italian seasoning, try soy sauce and ginger. Curry mix. Cumin and chili powder. Miso, vegemite, or marmite. Mustard and tarragon. Butter. Heavy cream. A fresh squeeze of lemon, a touch of cheese, or a small handful of slivered almonds just before eating.</p>
                <p>Add some bulk. For the last minute of cooking, add 4 ounces of cooked pasta or rice. You can stir that in or just move the vegetables to the side and put it right next to it in the same container. Black or refried beans. Mashed potatoes. Cooked quinoa. Corn kernels.</p>
                <p>Make a sauce. Mix some tomato paste or soup base with a little bit of water. If you'd like a thicker sauce, add some modified food starch, available online or elsewhere that molecular gastronomy supplies are sold.</p>
