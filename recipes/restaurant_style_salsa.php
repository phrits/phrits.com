            <h1>Restaurant Style Salsa</h1>
            <p>Nothing beats fresh salsa, and nothing beats &quot;I made that myself.&quot; It's easy to make, and it's a great use for tomatoes and peppers that have passed their prime. That's right. Everyone's favorite reason for going to a Mexican restaurant is really just a way for the restaurant to use up tomatoes and peppers they'd otherwise have to throw away.</p>

            <h2>Ingredients</h2>
            <ul>
                <li class="ingredient">5 lbs. fresh, whole tomatoes</li>
                <li class="ingredient">1 large green bell pepper</li>
                <li class="ingredient">0 or more hot peppers, as much of whatever variety you prefer</li>
                <li class="ingredient">1 medium onion</li>
                <li class="ingredient">6 garlic cloves</li>
                <li class="ingredient">1 can of tomatoes (See note.)</li>
                <li class="ingredient">juice of 1 or 2 limes</li>
                <li class="ingredient">1 bunch cilantro, thoroughly washed</li>
                <li class="ingredient">salt to taste</li>
            </ul>
            <p><span class="emph">Note:</span> If you are using tomatoes from your garden, you can skip the canned. But grocery store tomatoes are grown for color, durability, appearance: Everything but flavor. It doesn't matter if you use diced, petite diced, whole, or crushed, but the sad specimens from the produce department need a little boost.</p>
            <h2>Equipment</h2>
            <p>You'll need a blender of some sort. A hand blender is ideal.</p>
            <h2>Instructions</h2>
            <ol>
                <li>Heat the oven to 425&deg;F/220&deg;C.</li>
                <li>Break off and discard stems and leaves from the tomatoes and peppers. Trim the &quot;hair&quot; from the onion and peel the papery outer skin from the onion and garlic. Cut the onion in half.</li>
                <li>Put the tomatoes, onions, garlic, and peppers on a sheet pan in the upper middle part of the oven and roast for 30 minutes. The skins should be charring.</li>
                <li>Dump the roasted vegetables into a steep walled container--a Dutch oven is perfect--and add the canned tomatoes, cilantro (stems and all), some lime juice, and some salt. (You can chill everything now if you want and blend it later.)</li>
                <li>Blend until it's as smooth as you like.</li>
                <li>Taste and add more salt or acid as desired. Go a little heavy because once it's chilled, those flavors will be muted.</li>
                <li>Chill and serve.</li>
            </ol>
            <h2>Yield</h2>
            <p>Two or three quarts, so there is plenty to share. It will keep well in the refrigerator for at least a week, and it freezes pretty well indefinitely.</p>
