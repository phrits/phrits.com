            <h1><span class="foreign">Pernil</span> (Slow-Roasted Pork)</h1>
            <p>This traditionally Puerto Rican roast is easy to make and tastes astoundingly good.</p>
            <h2>Ingredients</h2>
            <ul>
                <li class="ingredient">1 Boston butt (pork shoulder). See note below.</li>
                <li class="ingredient">2 large onions</li>
                <li class="ingredient">1 head of garlic</li>
                <li class="ingredient">2 tablespoons dried thyme</li>
                <li class="ingredient">2 tablespoons black pepper</li>
                <li class="ingredient">1 tablespoon ground annato/<span class="foreign">achiote</span> (optional)</li>
                <li class="ingredient">3 jalape&ntilde;o peppers, stems removed</li>
                <li class="ingredient">1 large poblano pepper, stem removed</li>
                <li class="ingredient">1/4 cup salt</li>
                <li class="ingredient">1/2 cup (or a little more) vegetable oil</li>
                <li class="ingredient">1/2 cup white vinegar</li>
                <li class="ingredient">water as needed</li>
                <li class="ingredient">salt and pepper to taste</li>
            </ul>
            <h2>Instructions</h2>
            <ol>
                <li>Score the roast deeply on all sides, squares an inch or two big.</li>
                <li>Pulse the onion, garlic, peppers, salt, pepper, thyme, and annato together in a food processor.</li>
                <li>With the food processor running, add oil until you have a paste.</li>
                <li>Add the vinegar and mix well.</li>
                <li>Smear the paste all over and into the cuts on the roast.</li>
                <li>Place roast in a pan and add about half an inch of water.</li>
                <li>Cook in a low oven&mdash;250&deg;F/120&deg;C or so&mdash;for 5 to 10 hours depending on the size of the roast. The meat will be safe to eat much earlier, of course, but it will become more tender and succulent with time in the oven.</li>
                <li>Every half hour or 45 minutes while it's cooking, turn it over, spoon any pan glop back on top, and make sure there's still some water in the bottom.</li>
                <li>Remove from the oven and allow to rest for 15 minutes.</li>
                <li>Pull the meat off the bone and add salt and pepper to taste.</li>
            </ol>
            <h3>Note</h3>
            <p>Many hunks of meat at the grocery store have been injected with water or a salt solution. If you can find a roast that <span class="emph">has not</span> been, you'll get a much better result.</p>
            <h2>Yield</h2>
            <p>Figure about a pound of pre-cooked weight per person, but a bigger roast will go a little further. 5 pounds will feed up to 6, but a 10-pound roast will easily feed 12.</p>
