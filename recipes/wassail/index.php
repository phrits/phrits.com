            <h2>Ingredients</h2>
            <ul>
                <li class="ingredient">1 gallon apple cider</li>
                <li class="ingredient">2 bottles dry white wine</li>
                <li class="ingredient">2 cups rum</li>
                <li class="ingredient">2 oranges, sliced</li>
                <li class="ingredient">1 lemon, sliced</li>
                <li class="ingredient">5 sticks cinnamon</li>
                <li class="ingredient">5 whole cloves</li>
                <li class="ingredient">5 allspice berries</li>
                <li class="ingredient">2 star anise</li>
                <li class="ingredient">1 whole nutmeg, cracked or crushed</li>
            </ul>
            <h2>Instructions</h2>
            <p><span class="emph">Note</span>: Wassail will keep nicely in the refrigrator for a week or two, but if you leave the spices in it, they will continue to steep and make the brew bitter. If you're not going to be consuming the whole batch immediately, you can tie up the cinnamon, etc. in cheesecloth for easy removal, or you can strain them out before storage.</p>
            <ol>
                <li>Combine the cider, fruit, and spices in a large pot.</li>
                <li>Bring to a boil, then immediately reduce to a simmer.</li>
                <li>Simmer until reduced by about a quarter.</li>
                <li>Add the wine. Bring to boil. Reduce to simmer.</li>
                <li>Add the rum. Bring to boil. Turn off heat.</li>
                <li>Remove fruit and spices.</li>
                <li>Serve warm.</li>
            </ol>
            <h2>Yield</h2>
            <p>About 1&nbsp;1/2 gallons.</p>
