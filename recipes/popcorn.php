            <h1>Popcorn</h1>
            <h2>Ingredients</h2>
            <ul>
                <li class="ingredient">Popcorn. Amount varies by cookpot. See <a href="#notes">Notes</a> below.</li>
                <li class="ingredient">2 Tablespoons oil, approximately, to cover the bottom of the pot</li>
                <li class="ingredient">2 Tablespoon melted butter, at least</li>
                <li class="ingredient">very finely ground salt and pepper to taste</li>
            </ul>
            <h2>Instructions</h2>
            <ol>
                <li>Add the oil and three popcorn kernels to the pot. Cover and apply medium high heat.</li>
                <li>Wait. Listen for the third kernel to pop. Be patient. Shake the pan a little bit if it makes you feel better.</li>
                <li>When the third kernel pops, remove the lid and add the rest of the corn, then shake to coat the kernels. Replace the cover.</li>
                <li>As the kernels start to pop, hold the cover on and shake the pot up and down every 15 seconds or so. You want to keep all the kernels in the oil and most of the puffs out.</li>
                <li>As the popping slows, be ready to empty the pot into a large bowl.</li>
                <li>Remove the lid and dump the corn into the bowl.</li>
                <li>Apply melted butter and mix gently. See <a href="#notes">Notes</a> below.</li>
                <li>Sprinkle salt and pepper over the top and mix gently.</li>
                <li>Hold warm or serve at room temperature.</li>
            </ol>
            <h2>Yield</h2>
            <p>Serves 2 at the very least.</p>
            <a name="notes"><h2>Notes and Tips</h2></a>
            <ul>
                <li><span class="emph">How much popcorn</span>: Start with the pot you're going to use, clean and dry. Add a single layer of popcorn to the pot. There's your amount. Measure it, and you'll know how much to use for the next batch. My favorite cooking pot uses about 3/4&nbsp;cup&mdash;around 1/4&nbsp;pound&mdash;of my favorite organic kernels.</li>
                <li>The best way I've found to <span class="emph">apply the butter</span> is to melt it and put it in a squeeze bottle. One of those yellow ones like you'd see at a hot dog stand is perfect. If you don't have a squeeze bottle, just do your best to drizzle it. Squirt (or drizzle) all over, mix, repeat.</li>
                <li>The <span class="emph">best pot for making popcorn</span> has a heavy bottom and a tight-fitting lid. My favorite one is my 4- or 4&nbsp;1/2-quart Dutch oven with a 3/8&nbsp;inch thick bottom and an inset lid. My Mom's old Revere Ware Dutch oven works fine, too. Cast iron is heavy to shake, so I avoid it for popcorn.</li>
                <li><span class="emph">Grind the salt and spices very fine.</span> The finer the grind, the better it will stick to the popcorn. Use popcorn salt, or make your own using a spice grinder. Grind the pepper or other spices until they're dust. Sprinkle, mix, repeat.</li>
                <li><span class="emph">Get a huge bowl for mixing</span> if you can. If you can't, a clean paper grocery bag works very well. You're not really supposed to use trash bags for food, but&hellip; The more room you can give the popped kernels to move around and mix, the less they'll break in the process.</li>
                <li><span class="emph">Trust your instincts</span> with the heat. Every stove is different. On mine, I start a little hotter and finish a little cooler. You want to cook it fast&mdash;minimizing the popped kernel's time in that steamy environment&mdash;but you don't want to burn it. You'll learn your own stove with practice.</li>
                <li><span class="emph">Variations</span>: Salt and pepper are just a start! You can use your favorite herbs and spices, or chili powder, cinnamon and sugar, cocoa and cayenne. If you visit your local natural foods store, you should be able to find Nutritional Yeast: It makes a really tasty fake "white cheddar". (Don't grind it, just mix.) Pepper and Parmesan. Garlic and thyme. Bacon and honey. Caribbean jerk. Wasabi. Anything is possible!</li>
                <li>When you <span class="emph">shake the pot</span> during cooking, you're applying the the <a href="http://en.wikipedia.org/wiki/Brazil_nut_effect">Brazil Nut Effect</a>. That won't be on the test.</li>
                <li>If you're interested in reading more about popcorn, you might enjoy my article, <a href="/content/popcorn_post.php">The Popcorn Post</a>, here on <a href="/"><span class="phrits-invert">phrits.com</span></a>.</li>
                </ul>
                <h2>Updates 2023-01-08</h2>
                <ul>
                    <li><span class="emph">Maybe don't shake it.</span> The agitation of the popping itself causes the kernels to fall to the bottom. I've had equal success just leaving it alone.</li>
                    <li>Go a little <span class="emph">heavier on the oil</span>. It cooks a little better (espescially if you're not shaking it) and yields a richer popped kernel.</li>
                </ul>
