                <h2>Hot Quickle</h2>
                <p>This is a much smaller batch than the others because if you make it hot enough, most mere mortals will treat it as a condiment&mdash;try it instead of <span class="foreign">pico de gallo</span> sometime&mdash;rather than a side dish. You could use fewer hot peppers, of course, but where's the fun in that? For a light, easy, and highly flavorful lunch dish, serve Hot Quickle wrapped in a corn tortilla with avocado, fresh cilantro, and fresh lime.
                <p>Oh, and wear gloves when you're cutting the hot peppers or you'll hate yourself later.</p>
                <ul>
                    <li class="ingredient">1/4 small red cabbage, thinly sliced</li>
                    <li class="ingredient">1 small onion, thinly sliced</li>
                    <li class="ingredient">1 or 2 cloves of garlic, sliced paper thin or minced</li>
                    <li class="ingredient">2 or more fresh jalape&ntilde;o, serrano, habanero&mdash;careful!&mdash;and/or poblano peppers, thinly sliced</li>
                    <li class="ingredient">1/4 c. loosely packed fresh cilantro leaves, roughly chopped</li>
                    <li class="ingredient">1T salt for pickling</li>
                </ul>
                <p>After it's pickled and rinsed, add:</p>
                <ul>
                    <li class="ingredient">white vinegar and/or fresh lime juice to taste</li>
                    <li class="ingredient">salt and pepper to taste</li>
                </ul>

                <h2>Other Notes</h2>
                <p>I love my <a href="http://www.amazon.com/Harold-Import-Company-BN1-Mandoline/dp/B0000VZ57C/">mandolin</a>, but I've learned that not everything should be paper thin. About 1/8-inch (3mm) slices of onion mix better with the raw cabbage than thinner ones. The same goes for zucchini and the like. Slices of carrot or radish are firmer and less watery, though, and the thinner they are, the better. </p>
                <p>Incidentally, it turns out that it is possible to oversalt the pickle-in-progress, but the worst of that is that it's wasteful. If your quickle is too salty after rinsing but before you've added the vinegar, etc., you can soak it in a bowl of fresh water for a little while.</p>
                <p>With all the combinations I've tried, the original&mdash;just cabbage, cukes, and onion&mdash;is still close to my family's favorite. I've added some kale to my master recipe, though: The dark green color is striking against the paler cabbage, and the deeper slightly more bitter flavor adds a nice couterpoint to its slight sweetness. I also usually include some carrots julienned or sliced very, very thinly. The color just pops and the flavor goes well with all the rest of it.</p>
                <p>I haven't been completely pleased with some of my experiments: I found the oregano in the Italian Quickle to be a little distracting, for example, and the fresh grated ginger in the Asian Quickle brought a little more bitterness than I cared for. There's a lot to be said for personal taste, though, and your mileage may vary. Mix and match with things you like. Good stuff!</p>
