            <h1>Updated</h1>
            <p>Don't let it distract you from what follows, but I've slightly improved the <a href="/recipes/quickle/">base recipe</a> since this was first written. (Spoiler: Add some kale.)</p>

            <h1>Quickle Pickle</h1>
            <p>Simple: Cabbage, onion, cucumber, salt, vinegar. That's it. High flavor and essentially no fat or calories.</p>
            <h2>Ingredients</h2>
            <ul>
                <li class="ingredient">1 head of cabbage</li>
                <li class="ingredient">1 red onion</li>
                <li class="ingredient">2 cucumbers</li>
                <li class="ingredient">2 Tbsp. salt</li>
                <li class="ingredient">2 Tbsp. red wine vinegar or to taste</li>
                <li class="ingredient">additional salt to taste</li>
            </ul>
            <h2>Instructions</h2>
            <ol>
                <li>Wash and core the cabbage, then slice it as thinly as you can.</li>
                <li>In a colander over a bowl or sink, mix the 2T of salt into the cabbage</li>
                <li>Peel and slice the onion as thinly as you can. A mandolin works well (but not with the cabbage). Mix the onion into the salted cabbage.</li>
                <li>Peel (or don't) the cucumbers. Cut them lengthwise, then gently scrape out the seeds and pith. Cut as thinly as you can. Mix in with the salted cabbage and onions.</li>
                <li>Place a small plate or something similar on top of the cabbage mixture and weigh it down. (A gallon jug of water or a big can works nicely.) Allow to sit for 45 minutes to an hour.</li>
                <li>Remove the plate and rinse the cabbage mix under running water.</li>
                <li>Transfer to a plastic zipper bag or a large mixing bowl.</li>
                <li>Add red wine vinegar and salt to taste.</li>
                <li>Serve or refrigerate for up to a few days.</li>
            </ol>
            <h2>Note</h2>
            <p>The original name for this recipe was "Quick Pickled Cabbage", but one of my tongue-tied kids accidentally renamed it to something that doesn't scare off brassicaphobics quite as quickly. While <a href="http://www.copyright.gov/fls/fl122.html">recipes cannot be copyrighted</a>, I'm happy to give full credit to Mark Bittman for this one. <a href="http://www.amazon.com/How-Cook-Everything-Simple-Recipes/dp/0028610105"><span class="title"></span>How to Cook Everything</span></a> is worth your attention.</p>
