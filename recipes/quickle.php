            <h1>Quickle Pickle</h1>
            <p>This recipe is based on &quot;Quick Pickled Cabbage&quot; from Mark Bittman's <a href="http://www.amazon.com/How-Cook-Everything-Simple-Recipes/dp/0028610105"><i>How to Cook Everything</i></a>. It's a book worth your attention.</p>
            <h2>Ingredients</h2>
            <ul>
                <li class="ingredient">1 medium head of cabbage</li>
                <li class="ingredient">6 to 8 ounces kale</li>
                <li class="ingredient">1 red onion</li>
                <li class="ingredient">2 cucumbers</li>
                <li class="ingredient">2 Tbsp. salt</li>
                <li class="ingredient">3 Tbsp. red wine vinegar or to taste</li>
                <li class="ingredient">additional salt to taste</li>
            </ul>
            <h2>Instructions</h2>
            <ol>
                <li>Wash and core the cabbage, then slice it as thinly as you can.</li>
                <li>In a colander over a bowl or sink, mix the 2T of salt into the cabbage</li>
                <li>Peel and slice the onion as thinly as you can. A mandolin works well for everything but the cabbage. Mix the onion into the salted cabbage.</li>
                <li>Peel (or don't) the cucumbers. Cut them lengthwise, then gently scrape out the seeds and pith. Cut as thinly as you can. Mix in with the salted cabbage and onions.</li>
                <li>Place a small plate or something similar on top of the cabbage mixture and weigh it down. A gallon jug of water or a big can works nicely. Allow to sit for 45 minutes to an hour.</li>
                <li>Remove the plate and rinse the cabbage mix under running water.</li>
                <li>Transfer to a plastic zipper bag or a large mixing bowl.</li>
                <li>Add red wine vinegar and salt to taste.</li>
                <li>Serve immediately or refrigerate for up to a few days.</li>
            </ol>
            <h2>Yield</h2>
            <p>Serves at least 12.</p>
