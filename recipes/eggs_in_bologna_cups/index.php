            <h1>Eggs in Bologna Cups</h1>
            <p>This recipe from my childhood&mdash;maybe the first I ever learned&mdash;has become the centerpiece of my family's Christmas Breakfast tradition. It's an easy way to make eggs for a crowd, and kids love them.</p>
            <h2>Ingredients</h2>
            <ul>
                <li class="ingredient">12 slices of bologna</li>
                <li class="ingredient">12 large eggs</li>
                <li class="ingredient">3/4 cup heavy cream or milk</li>
                <li class="ingredient">Salt, Pepper, and Paprika to taste</li>
