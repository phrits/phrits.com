            <h2>Instructions</h2>

<img class="image right" src="/images/cooking_with_gramp.jpg" alt="Cooking with Gramp" title="Helloooo, Balogna!" />

                <p>The first step used to be to fry each slice of bologna briefly, causing the center to puff up and form a natural cup. Modern grocery store bologna&mdash;even the name brands I've tested&mdash;doesn't seem to do that anymore, so I don't usually bother. Unless, of course, I'm looking for an excuse to bring a grandchild into the kitchen to help.</p>
            <ol>
                <li>Place each slice of bologna into one of the cups of a buttered, cooking sprayed, or nonstick muffin pan. It may take a little finesse to get it to stay, so be careful not to poke a hole through the bottom of the bologna slice.</li>
                <li>Break an egg into each cup.</li>
                <li>To each egg, add 1T of heavy cream.</li>
                <li>Add salt, pepper, and paprika to each.</li>
                <li>Bake at 350&deg;F/175&deg;C for about 20 minutes or until the egg has set.</li>
                <li>Using a rubber spatula, remove the bologna cups to a platter and serve.</li>
            </ol>
            <h2>Serving</h2>
            <p>Serve warm or at room temperature. They are also surprisingly good leftover cold, straight from the refrigerator. I suspect they would be quite nice, hot or cold, on toast with mayonnaise or mustard, lettuce, tomato, and a slice of cheese.</p>
            <h2>Yield</h2>
            <p>12 each.</p>
