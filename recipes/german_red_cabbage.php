            <h1>German Red Cabbage</h1>
            <p>I've read that this recipe can be made with goose fat, but I've never had access to it. Made with lard, it goes tremendously well with pork.</p>
            <h2>Ingredients</h2>
            <ul>
                <li class="ingredient">1/4 cup lard</li>
                <li class="ingredient">1 large onion, finely chopped</li>
                <li class="ingredient">1 large red cabbage, cored and sliced very thin</li>
                <li class="ingredient">4 small apples, peeled, cored, and chopped</li>
                <li class="ingredient">2 cups water, fruity red wine, or a mixture</li>
                <li class="ingredient">2 bay leaves</li>
                <li class="ingredient">3 or 4 whole cloves</li>
                <li class="ingredient">1 teaspoon salt or to taste</li>
                <li class="ingredient">1 Tablespoon sugar or to taste</li>
                <li class="ingredient">1 teaspoon cornstarch, mixed in a little water</li>
                <li class="ingredient">1/4 cup apple cider or red wine vinegar or to taste</li>
                <li class="ingredient">1 Tablespoon red currant jelly (optional)</li>
            </ul>
            <h2>Instructions</h2>
            <ol>
                <li>Melt the lard and sweat the onion until it is pale yellow.</li>
                <li>Add the cabbage, stir to coat, then cook just until it begins to soften.</li>
                <li>Mix in the apples.</li>
                <li>Add the bay leaf, salt, and water.</li>
                <li>Cook for about an hour until the cabbage is quite soft.</li>
                <li>Add the sugar and vinegar, then thicken with the cornstarch dissolved in the tablespoon of water.</li>
                <li>If desired, add the red currant jelly.</li>
            </ol>
            <h2>Yield</h2>
            <p>Serves at least 10. It's as good or better refrigerated and served the next day. It also freezes well for several weeks.</p>
