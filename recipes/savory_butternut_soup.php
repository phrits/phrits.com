            <h1>Savory Butternut Squash Soup</h1>

            <h2>Ingredients</h2>

            <ul>

                <li class="ingredient">1 medium butternut squash, peeled, seeded, and cut into 1 to 1&frac12;-inch (2 to 4 cm) chunks</li>

                <li class="ingredient">4 strips of thick cut bacon, chopped. See note on Dietary Considerations below.</li>

                <li class="ingredient">1 medium onion, finely chopped</li>

                <li class="ingredient">1 small carrot, finely chopped</li>

                <li class="ingredient">1 stalk of celery, finely chopped</li>

                <li class="ingredient">1 clove of garlic, finely chopped</li>

                <li class="ingredient">(Optional) 1 habenero or scotch bonnet pepper, seeded and very finely chopped. Wear gloves or wish you had.</li>

                <li class="ingredient">1/4 cup (75 ml or 30 g) flour</li>

                <li class="ingredient">a pinch of cayenne or a few drops of hot sauce</li>

                <li class="ingredient">2 quarts (2 L), more or less, of stock. See note on Stock below.</li>

                <li class="ingredient">1 whole sprig fresh thyme (about 1/3 of a grocery store US$2 blister pack)</li>

                <li class="ingredient">1 whole sprig fresh oregano</li>

                <li class="ingredient">1 sprig of fresh rosemary (about 4 inches (10 cm))</li>

                <li class="ingredient">3 fresh bay leaves</li>

                <li class="ingredient">(Optional)3 to 12 fresh jalape&ntilde;o peppers, seeded and roughly chopped</li>

                <li class="ingredient">1&nbsp;1/2 cup (375 ml) heavy cream, plus another 1/2 cup (125 ml) to finish. A pint (500 ml) total</li>

                <li class="ingredient">Salt and pepper</li>

                <li class="ingredient">(Optional) Garnishes</li>

            </ul>

            <h3>Note: Dietary Considerations</h3>

            <p>This soup features a vegetable, and the animal-based products play a supporting role. If you prefer to leave them out entirely, it's easily done. Any food-grade vegetable oil can replace the bacon, for example. The cream can be omitted, or replaced by one of the plant-based alternatives. If you eschew wheat or gluten, change or omit the flour called for above. It won't be the same, but it will be good. Soup is a pretty forgiving food.</p>

            <h3>Note: Stock</h3>

            <p>Homemade chicken stock is really what you want here, but vegetable stock is also very good and makes the soup <span class="dietary">Vegetarian</span> friendly. Most of the grocery store stocks are good enough for the purpose. Water will do if that's what you have.</p>

            <h3>Note: Herbs</h3>

            <p>Use more of what you like, less of what you don't. If you don't have fresh, dried will usually do, and vice versa. Use a lot more than what most recipes call for.</p>

            <h2>Equipment</h2>

            <p>An electric stick blender makes this a really easy and elegant soup to make. You can use a countertop blender, too, but be extra careful and start with much smaller batches than you'd expect: Hot soup expands quickly when you blend it, and it's easy to end up with a hot, dangerous mess.</p>

            <p>Make sure your pot is big enough. You'll want room to stir things.</p>

            <h2>Instructions</h2>

            <ol>

                <li>In a large pot, cook the bacon over medium-low heat. Stir it occasionally to keep it from sticking, and cook until the fat is rendered and the meaty bits are crisp.</li>

                <li>Add the onion, carrot, celery and garlic. Add a good pinch of salt, and mix.</li>

                <li>Sweat the vegetables in the oil until the onions are transluscent.</li>

                <li>Sprinkle the flour over the vegetables and stir to mix. Cook for a few minutes as the starches thicken everything into a mass.</li>

                <li>Add a little stock and stir it in. Add more as it will take it until you have a smooth liquid&mdash;a thin batter or thick gravy&mdash;putting in that little bit of cayenne or hot sauce along the way.</li>

                <li>Add the squash and a good heavy couple of pinches of salt. Stir. Add enough stock to cover, and stir.</li>

                <li>(Optional) Tie the herb branches and bay leaves together with some kitchen twine, leaving a long tail that you can then drape over the pot handle. That makes removing the woody parts out of soup later on.</li>

                <li>Add the herbs. (If you're using dried, go ahead and mix.)</li>

                <li>Bring to a boil, then immediately reduce to a simmer.</li>

                <li>Cook for half an hour to 45 minutes, until you can easily pierce a chunk of the squash with the tip of a paring knife.</li>

                <li>(Optional, before the following Recommended step) Pull out half to a third of the pieces of squash and set them aside for the moment.</li>

                <li>(Recommended) Blend or put the sauce through a food mill until the sauce is smooth and uniform rather than chunky.</li>

                <li>(Optional) Add the jalape&ntilde;o peppers.</li>

                <li>Bring back up to a boil, then reduce to a simmer. Let it cook for a few minutes, stirring only as needed.</li>

                <li>Stir in the larger portion of the heavy cream, and bump the heat a bit to bring the soup back up to a simmer.</li>

                <li>If you pulled some of the whole pieces aside above, gently return them to the soup.</li>

                <li>Taste and add salt. Repeat as necessary.</li>

                <li>Bring back up to a boil, then reduce to a simmer and let cook for 10 minutes or so.</li>

                <li>Hold warm until you're ready to serve it.</li>

                <li>Remove from heat and stir in the rest of the heavy cream.</li>

                <li>Garnish with any or all of sour cream, saut&eacute;ed mushrooms, crumbled bacon, chives, fresh herbs, sliced jalabe&ntilde;os, etc.</li>

            </ol>

            <h2>Variations</h2>

            <p>This is essentially my interpretation&mdash;I cheated on the roux, for example&mdash;of the "cream of anything" soup recipe taught early in most culinary programs. As such, with a few tweaks to flavors and the blending operation, you can replace the butternut squash with potatoes or celery or broccoli or chicken or beets or mushrooms or ... The approach works for just about anything.</p>

            <h2>Yield</h2>

            <p>Makes about 2 quarts.</p>

