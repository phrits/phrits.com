            <h1>London Broil</h1>
            <p>I remember when my Mom first started cooking this dish, then watched it move to my Dad's grill. I think they first had it at Aunt Kathy's table, which has always been one of my own favorite places to eat.</p>
            <p>London Broil is traditionally marinated&mdash;as when I first tried it&mdash;but I prefer the sear I get with a dry rub. I've listed the flavors I typically use, but I've also had great success including other flavors such as coffee or chili powder, so don't feel constrained: I don't even know what the "authentic" version calls for.</p>
            <h2>Ingredients</h2>
            <ul>
                <li class="ingredient">1 beef Top Round ("London Broil") cut, 1 to 2&nbsp;1/2 inches thick, 1&nbsp;1/2 to 5 pounds</li>
                <li class="ingredient"><a href="#dry_rub">Dry Rub</a> or <a href="#marinade">Marinade</a> (below)</li>
            </ul>
            <h3><a name="dry_rub"></a>Dry Rub</h3>
            <ul>
                <li class="ingredient">Kosher Salt to cover, about 1 Tablespoon per pound of beef</li>
                <li class="ingredient">3 Tbsp. granulated garlic</li>
                <li class="ingredient">2 Tbsp. granulated onion</li>
                <li class="ingredient">1 Tbsp. paprika</li>
                <li class="ingredient">1 Tbsp. freshly ground black pepper</li>
            </ul>
            <h4>Preparation</h4>
            <ol>
                <li>Sprinkle the salt all over the meat: Top, bottom, and sides. Set aside.</li>
                <li>Combine the other ingredients. You can proceed, but you'll get even more flavor in the beef if you just let it sit with the salt for half an hour or so first.</li>
                <li>Sprinkle the mixture all over the meat: Top, bottom, and sides. Set aside for an hour or longer, refrigerating if it's going to be more than a couple of hours.</li>
            </ol>
            <h3><a name="marinade"></a>Marinade</h3>
            <ul>
                <li class="ingredient"><a href="#dry_rub">Dry Rub</a> ingredients, above, doubled</li>
                <li class="ingredient">3/4 cup vegetable oil</li>
                <li class="ingredient">3/4 cup dry red wine, beer, beef stock, soy sauce (skip the Kosher salt!), or water</li>
            </ul>
            <h4>Preparation</h4>
            <ol>
                <li>Combine all marinade ingredients in a bowl.</li>
                <li>Cover the beef in the marinade in a flat dish or plastic bag.</li>
                <li>Refrigerate for three to four hours or longer.</li>
                <li>Remove from the fridge and take it out of the marinade half an hour before cooking. Pat it dry with paper towels.</li>
            </ol>
            <h2>Cooking Instructions</h2>
            <ol>
                <li>Sear on both sides using high heat, about 2 minutes per side.</li>
                <li>Roast (bake) or grill at 325&deg;F/160&deg;C until rare.</li>
                <li>Let rest for at least 10 minutes.</li>
                <li>Slice thinly across the grain and serve.</li>
            </ol>
            <h2>Yield</h2>
            <p>By weight. Figure about 3 people per pound, less if served family style.</p>
