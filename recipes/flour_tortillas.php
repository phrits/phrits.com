            <h1>Flour Tortillas</h1>
            <p>The real secret to these is to use lard. Butter or shortening will work&mdash;and they'll still be better than what you'll find at the grocery store or most American "Mexican" restaurants&mdash;but they won't be nearly as good.</p>
            <h2>Ingredients</h2>
            <ul>
                <li class="ingredient">1 1/2 teaspoons baking powder</li>
                <li class="ingredient">3/4 cup warm water</li>
                <li class="ingredient">1/4 cup lard</li>
                <li class="ingredient">1 teaspoon salt</li>
                <li class="ingredient">2 cups unbleached all-purpose flour</li>
            </ul>
            <h2>Instructions</h2>
            <ol>
                <li>Mix the flour, salt, and baking powder in a bowl.</li>
                <li>Cut the lard into the dry ingredients using a pastry blender.</li>
                <li>Slowly stir in the water.</li>
                <li>Knead for 3 to 5 minutes until the dough is elastic.</li>
                <li>Place in a greased bowl and cover tightly.</li>
                <li>Allow the dough to rest for one hour.</li>
                <li>Pinch off a 1-inch piece of dough, shape it into into a ball, and roll it flat--about 7 inches around and very thin.</li>
                <li>Cook on a hot cast iron skillet until bubbly and starting to brown in spots, then flip it over and cook the other side for another 15 to 20 seconds.</li>
                <li>Remove it to a basket or tortilla holder.</li>
                <li>Repeat from step 7 until you have used all the dough.</li>
            </ol>
            <h2>Yield</h2>
            <p>Serves 6 to 8. Less if you use lard or don't make them every day.</p>
