            <h1>Pumpkin Mini-muffins</h1>
            <p>Everyone has a pumpkin muffin or bread recipe, and they're all pretty close to each other. Here's my variation on the theme.</p>

            <h2>Ingredients</h2>
            <ul>                    
                <li class="ingredient">2 cups unbleached all-purpose flour</li>
                <li class="ingredient">3/4 teaspoon salt</li>
                <li class="ingredient">1 teaspoon baking soda</li>
                <li class="ingredient">1/2 teaspoon baking powder</li>
                <li class="ingredient">1/2 teaspoon mace or ground nutmeg</li>
                <li class="ingredient">1/4 teaspoon ground cloves</li>
                <li class="ingredient">1/4 teaspoon grown cardamom</li>
                <li class="ingredient">1/2 teaspoon ground ginger</li>
                <li class="ingredient">1/4 teaspoon ground allspice</li>
                <li class="ingredient">3 teaspoons cinnamon, divided</li>
                <li class="ingredient">1/2 cup brown sugar</li>
                <li class="ingredient">1 3/4 cups white sugar</li>
                <li class="ingredient">2 eggs</li>
                <li class="ingredient">3/4 cups butter</li>
                <li class="ingredient">1 can (15 oz.) canned pumpkin puree</li>
            </ul>

            <p><span class="emph">Note:</span> If you have a favorite pumpkin spice mix or don't have everything called for&mdash;ground cardamom isn't on most home bakers' shelves, and it's probably not worth buying for just the tiny amount called for here&mdash;you can safely make up the difference with one of the others. A little less than a tablespoon of total spices is about right.</p>

            <h2>Instructions</h2>
            <ol>
                <li>Heat the oven to 350&deg;F/175&deg;C.</li>
                <li>Grease the muffin pan well.</li>
                <li>Whisk or sift together the flour, baking powder, baking soda, and spices. Set these dry ingredients aside.</li>
                <li>Combine the brown sugar and 2 teaspoons of the cinnamon. Set aside.</li>
                <li>Cream together the sugar, eggs, and butter until the mixture is light and fluffy.</li>
                <li>Mix in the pumpkin.</li>
                <li>Fold in the dry ingredients and mix just until incorporated.</li>
                <li>Fill mini-muffin cups about 2/3 full. (Remember to use water for any empties you have in the pan.)</li>
                <li>Sprinkle the brown sugar mixture on top, about 1/2 teaspoon for each.</li>
                <li>Bake for about 15 minutes or until a toothpick inserted into a muffin comes out clean, or with only a few cooked crumbs sticking to it.</li>
                <li>Remove from pan and allow to cool on a rack.</li>
            </ol>
            <h2>Yield</h2>
            <p>Makes at least 4 dozen.</p>
