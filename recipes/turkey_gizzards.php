            <h1>Turkey Gizzards</h1>
            <h2>Ingredients</h2>
            <h3>Simmer</h3>
            <ul>
                <li class="ingredient">2 pounds turkey gizzards</li>
                <li class="ingredient">1 Tablespoon thyme</li>
                <li class="ingredient">2 bay leaves</li>
                <li class="ingredient">1 Tablespoon black peppercorns</li>
                <li class="ingredient">2 whole cloves</li>
                <li class="ingredient">3 cloves garlic</li>
                <li class="ingredient">1 medium onion</li>
                <li class="ingredient">1 stalk celery</li>
                <li class="ingredient">1 small carrot</li>
            </ul>
            <h3>Fry</h3>
            <ul>
                <li class="ingredient">2 cups flour, approximately</li>
                <li class="ingredient">fine salt</li>
                <li class="ingredient">fresh ground pepper</li>
                <li class="ingredient">dried sage</li>
                <li class="ingredient">garlic powder</li>
                <li class="ingredient">3 cups cooking oil, approximately</li>
                <li class="ingredient">1 egg</li>
                <li class="ingredient">2 cups milk, approximately</li>
            </ul>
            <h2>Instructions</h2>
            <ol>
                <li>In a large pot, rinse the gizzards in a few changes of water until the water is clear.</li>
                <li>Wash, trim, and peel the garlic, onions, celery, and carrot.</li>
                <li>Add the produce, herbs, and spices to the pot with the gizzards and add water to cover by an inch or two.</li>
                <li>Bring to boil, reduce to simmer, and cook until very tender. This will take a couple of hours, and you may have to add water.</li>
                <li>Drain and cool. Discard the aromatics, but reserve the turkey broth for another use. (You'll have a very flavorful broth. We don't need it here, but it would be a real shame to just throw it away.)</li>
                <li>Slice the gizzards about 1/4-inch thick.</li>
                <li>Mix the flour and seasonings to taste.</li>
                <li>Beat the egg and mix it into the milk.</li>
                <li>In a deep pot, heat the oil to 375&deg;F. The pot should not be more than about half full, and the oil should be at least two inches deep.</li>
                <li>In small batches, using the <a href="https://www.thespruceeats.com/the-standard-breading-procedure-995453">standard breading procedure</a> with the seasoned flour and milk mixture above, fry the gizzard slices until golden brown.
                <li>Serve at any temperature.</li>
            </ol>
            <p><span class="emph">Note:</span> Gizzards are very tough, so they need the long cooking time to tenderize. Even the gristle should be soft. If they're not cooked long enough to prevent it, the gizzards become chewy as they cool. Still really tasty, just not as good as they could be.</p>
            <h2>Yield</h2>
            <p>Serves 6 as a main course or a small crowd as an appetizer.</p>
