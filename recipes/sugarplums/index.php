            <p>Sugarplums are simple and old-fashioned: Chopped nuts and dried fruits, spiced, and held together with honey. You can use whatever specific ingredients you like, so if you don't like walnuts but love cashews, for example, build your mix accordingly. If you have access to a good bulk foods store, you can buy exactly&mdash;and only&mdash;what you want.</p>
            <p>We who have grown up with candy bars at the checkout counter, lollipops through the bank drive-through, and mints on the way out of the restaurant are used to candies that are very sweet. Before industrially refined sugar became commonplace, though, these confections would dance in the dreams of children awaiting <span class="title"><a href="http://www.poets.org/viewmedia.php/prmMID/19286">A Visit from Saint Nicholas</a></span>. They're sweet without being obnoxious about it, and they're a healthful food besides.</p>
            <h2>Ingredients</h2>
            <ul>For your mix, use about equal amounts of nuts and fruits:
                <li class="ingredient">mixed nuts; e.g., pecans, almonds, pistachios, cashews, Brazil nuts, walnuts, macadamia nuts, peanuts</li>
                <li class="ingredient">mixed dried fruits; e.g., apricots, apples, prunes, dates, cherries, berries, pineapple, candied ginger, pears, </li>
                <li class="ingredient">dried ground spices; e.g., cinnamon, cardamom, star anise, ginger, clove, allspice, nutmeg, mace</li>
                <li class="ingredient">honey</li>
                <li class="ingredient">coarse or decorative sugar. Optional: If you leave it out, (and don't toast the nuts), the treats are also friendly for folks eating <span class="dietary">Raw</span> diets.</li>
            </ul>
            <h2>Instructions</h2>
            <div class="insetLeft">
                <a href="/images/sugarplum.jpg" alt="Sugarplums" title="Sugarplums"><img src="/images/sugarplum_small.jpg" alt="Sugarplum" title="Sugarplum"></img></a>
            </div> <!-- class="insetLeft" -->
            <ol>A food processor makes all the chopping incredibly quick and easy to do, but a knife is just fine.
                <li>Toast any raw nuts that are better that way&mdash;almonds and pecans, for example, but maybe not Brazils or macadamias. Allow to cool.</li>
                <li>With quick pulses of the food processor, chop the nuts. For best results, only chop one kind of nut at a time and use quick pulses. You don't want to make nut butter or pulverize them into dust, but smaller pieces stick together better than large ones. Pieces between 1/8" and 1/4" are ideal.</li>
                <li>Using the food processor, chop the fruits. Again, one type of fruit at a time is the best way to go. Dried fruits are sticky, though, so use longer pulses or just run the machine for 10 to 15 seconds at a time. As with the nuts, 1/8" to 1/4" pieces are just right.</li>
                <li>Combine the fruits and nuts in a big bowl, add some spices&mdash;not too much at first&mdash;and mix. Taste your mixture and add more spices if you like. Taste and add until you like the balance.</li>
                <li>Mix in enough honey to make everything sticky enough to hold together. The amount you need will vary by how much mixture is in the bowl, how finely chopped the nuts are, and how sticky the dried fruits are. As with the spices, add less than you think you'll need at first, then adjust.</li>
                <li>To shape the sugarplums, take a small amount&mdash;one or two bites&mdash;of the mix out of the bowl and roll it into a ball.</li>
                <li>(Optional.) Roll the ball in sugar.</li>
                <li>Place the sugarplum on a sheet of parchment or waxed paper to dry for several hours to a couple of days, mostly depending on humidity. Rolled in sugar, they'll develop a nice crunch on the outside.</li>
                <li>Repeat the shaping and rolling for the rest of the mixture.</li>
            </ol>
            <h2>Yield</h2>
            <p>Varies by amount of food and size of the sugarplum.</p>
            <h2>Storage</h2>
            <p>None of the ingredients require refrigeration. Once the sugarplums have dried, store them in an airtight container. They'll keep for several weeks.</p>
