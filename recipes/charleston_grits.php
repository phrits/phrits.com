            <h1>Charleston Grits</h1>
            <h2>Ingredients</h2>
            <ul>
                <li class="ingredient">3 cups water</li>
                <li class="ingredient">3 cups milk, whole preferred</li>
                <li class="ingredient">2 teaspoons salt or to taste</li>
                <li class="ingredient">1/2 cup (1 stick) butter</li>
                <li class="ingredient">1 cup grits, stone ground preferred</li>
                <li class="ingredient">1 cup cream</li>
            </ul>
            <h2>Instructions</h2>
            <ol>
                <li>Combine the milk, water, salt, and butter in a large saucepan.</li>
                <li>Over medium heat, bring just barely to a boil.</li>
                <li>Add the grits while stirring, being careful to avoid lumps.</li>
                <li>Return to a boil, stirring frequently, then reduce to a simmer.</li>
                <li>Cook until just past <span class="foreign">al dente</span>, twenty minutes or more, stirring frequently. "Quick" grits will take less time.</li>
                <li>Add the cream and cook for another 5 or 10 minutes.</li>
                <li>Serve hot with additional butter, salt, and pepper.</li>
            </ol>
            <h2>Yield</h2>
            <p>Serves 6.</p>
