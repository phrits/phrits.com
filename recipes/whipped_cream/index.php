            <h1>Whipped Cream</h1>
            <p>Some of the best things are the simplest. Cream. Sugar. Done.</p>
            <h2>Ingredients</h2>
            <ul>
                <li class="ingredient">heavy cream (See note.)</li>
                <li class="ingredient">sugar (See note.)</li>
            </ul>
            <h3>Notes</h3>
            <p><span class="emph">heavy cream</span>: In the US, we have a few products called variously "heavy" or "whipping" cream, and they vary in their fat content between 40g and 50g per serving. Any of them will do, but if you have a choice, go for the higher fat content.</p>
            <p><span class="emph">sugar</span>: You may have seen recipes calling for "casters sugar". That only means that it's ground very finely. If you have a coffee grinder (especially one you keep set aside for spices), you can make your own in seconds. Fine sugar prevents any possibility of grittiness, but that's seldom anything to worry about. The stuff in your pantry will do fine.</p>
            <h2>Equipment</h2>
            <p><span class="emph">Good</span>: A hand whisk (or "whip") and a big bowl. Persevere and you will be rewarded. Things start slowly, but once the cream finally starts to whip up, you're almost done.</p>
            <p><span class="emph">Better</span>: A handheld electric mixer. If you have a balloon whisk attachment, that's better still.</p>
            <p><span class="emph">Best</span>: A stand mixer with a whisk attachment. Crank it up and don't forget about it or you may overwhip the cream.</p>
            <h2>Instructions</h2>
            <p><span class="emph">Hint: </span>Keep your cream cold until you're ready to use it. If you have space in your freezer to chill the bowl, whisk, and sugar, even better. But that's probably overkill unless your kitchen is especially hot.</p>
            <ol>
                <li>Pour the cream into a large bowl.</li>
                <li>Whisk vigorously until the cream starts to thicken and expand, 3 to 10 minutes.</li>
                <li>Add sugar to taste.</li>
                <li>Continue whisking to the soft peak stage. (If you touch the cream with a spoon and take it away, the cream should pull on the spoon a little bit, leaving a tiny little peak that falls over at its tip.)</li>
            </ol>
            <p><span class="emph">Hint: </span>If you find that the cream starting to thicken more than you like, you can sort of "reverse" the whipping process by adding in a little bit of unwhipped cream.</p>
            <h2>Serve</h2>
            <p>Keep it cold and serve immediately if you can. It's still great on leftover pie even a couple of days later, but it's at its freshest and most elegant right after it's made.</p>
            <h2>Yield</h2>
            <p>Makes about four times your starting volume: A pint of heavy cream will yield about half a gallon of whipped cream.</p>
            <h2>Variations</h2>
            <h3>Flavored Whipped Cream</h3>
            <p>After whipping, fold in vanilla or almond extract; espresso powder or finely ground coffee; cocoa powder; or mint.</p>
            <h3>Colors</h3>
            <p>After whipping, fold in a few drops of food coloring.</p>
            <h3>Textures</h3>
            <p>After whipping, fold in chopped nuts or fruit, crumbled cookies, or bits of toffee.</p>
            <h3><span class="phrits-invert">phaux</span> Strawberry Mousse</h3>
            <p>Chop fresh strawberries and let them macerate in a little sugar. Drain, reserving the liquid. Fold the strawberries into the whipped cream. Spoon or pipe into bowls and drizzle the reserved liquid over top. Garnish and serve.</p>
            <h3><span class="phrits-invert">phaux</span> Chocolate Mousse</h3>
            <p>Melt unsweetend chocolate with a little heavy cream or unsalted butter and allow to cool. Shave some semi-sweet or milk chocolate into curls. Fold melted and shaved chocolate into the whipped cream. Spoon or pipe into bowls and garnish with one or more of shaved chocolate, cocoa powder, chocolate sauce, and miniature chocolate chips. This one makes a nice topping for hot chocolate, too.</p>
            <h3><span class="phrits-invert">phaux</span> Pumpkin Mousse</h3>
            <p>In a separate bowl, combine canned, pur&eacute;ed pumpkin, a double helping of ground pumpkin spices&mdash;say, a cup of pumpkin and two tablepoons or a little more spice per pint of cream&mdash;and maybe a little brown sugar. (You can even whip in some air if you want.) Fold into whipped cream. Spoon or pipe into bowls and garnish with pie crust cutouts or shortbread cookies</p>
